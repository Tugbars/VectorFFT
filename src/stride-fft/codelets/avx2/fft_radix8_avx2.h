__attribute__((target("avx2,fma")))
static inline void
radix8_tw_log3_dit_kernel_fwd_avx2(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    const double * __restrict__ base_tw_re, const double * __restrict__ base_tw_im,
    size_t K)
{
    const __m256d vc = _mm256_set1_pd(0.707106781186547572737310929369414225220680236816);
    const __m256d vnc = _mm256_set1_pd(-0.707106781186547572737310929369414225220680236816);

    for (size_t k = 0; k < K; k += VL) {
        /* Log3: load W^1, W^2, W^4 bases */
        const __m256d b1r = LD(&base_tw_re[0*K+k]), b1i = LD(&base_tw_im[0*K+k]);
        const __m256d b2r = LD(&base_tw_re[1*K+k]), b2i = LD(&base_tw_im[1*K+k]);
        const __m256d b4r = LD(&base_tw_re[2*K+k]), b4i = LD(&base_tw_im[2*K+k]);

        /* Derive W^3 = W^1 * W^2 */
        const __m256d w3r = _mm256_fnmadd_pd(b1i,b2i,_mm256_mul_pd(b1r,b2r));
        const __m256d w3i = _mm256_fmadd_pd(b1r,b2i,_mm256_mul_pd(b1i,b2r));
        /* Derive W^5 = W^1 * W^4 */
        const __m256d w5r = _mm256_fnmadd_pd(b1i,b4i,_mm256_mul_pd(b1r,b4r));
        const __m256d w5i = _mm256_fmadd_pd(b1r,b4i,_mm256_mul_pd(b1i,b4r));
        /* Derive W^6 = W^2 * W^4 */
        const __m256d w6r = _mm256_fnmadd_pd(b2i,b4i,_mm256_mul_pd(b2r,b4r));
        const __m256d w6i = _mm256_fmadd_pd(b2r,b4i,_mm256_mul_pd(b2i,b4r));
        /* Derive W^7 = W^3 * W^4 */
        const __m256d w7r = _mm256_fnmadd_pd(w3i,b4i,_mm256_mul_pd(w3r,b4r));
        const __m256d w7i = _mm256_fmadd_pd(w3r,b4i,_mm256_mul_pd(w3i,b4r));

        const __m256d x0r = LD(&in_re[k]), x0i = LD(&in_im[k]);
        __m256d x1r = LD(&in_re[1*K+k]), x1i = LD(&in_im[1*K+k]);
        { const __m256d tr = x1r;
          x1r = _mm256_fnmadd_pd(x1i,b1i,_mm256_mul_pd(tr,b1r));
          x1i = _mm256_fmadd_pd(tr,b1i,_mm256_mul_pd(x1i,b1r)); }
        __m256d x2r = LD(&in_re[2*K+k]), x2i = LD(&in_im[2*K+k]);
        { const __m256d tr = x2r;
          x2r = _mm256_fnmadd_pd(x2i,b2i,_mm256_mul_pd(tr,b2r));
          x2i = _mm256_fmadd_pd(tr,b2i,_mm256_mul_pd(x2i,b2r)); }
        __m256d x3r = LD(&in_re[3*K+k]), x3i = LD(&in_im[3*K+k]);
        { const __m256d tr = x3r;
          x3r = _mm256_fnmadd_pd(x3i,w3i,_mm256_mul_pd(tr,w3r));
          x3i = _mm256_fmadd_pd(tr,w3i,_mm256_mul_pd(x3i,w3r)); }
        __m256d x4r = LD(&in_re[4*K+k]), x4i = LD(&in_im[4*K+k]);
        { const __m256d tr = x4r;
          x4r = _mm256_fnmadd_pd(x4i,b4i,_mm256_mul_pd(tr,b4r));
          x4i = _mm256_fmadd_pd(tr,b4i,_mm256_mul_pd(x4i,b4r)); }
        __m256d x5r = LD(&in_re[5*K+k]), x5i = LD(&in_im[5*K+k]);
        { const __m256d tr = x5r;
          x5r = _mm256_fnmadd_pd(x5i,w5i,_mm256_mul_pd(tr,w5r));
          x5i = _mm256_fmadd_pd(tr,w5i,_mm256_mul_pd(x5i,w5r)); }
        __m256d x6r = LD(&in_re[6*K+k]), x6i = LD(&in_im[6*K+k]);
        { const __m256d tr = x6r;
          x6r = _mm256_fnmadd_pd(x6i,w6i,_mm256_mul_pd(tr,w6r));
          x6i = _mm256_fmadd_pd(tr,w6i,_mm256_mul_pd(x6i,w6r)); }
        __m256d x7r = LD(&in_re[7*K+k]), x7i = LD(&in_im[7*K+k]);
        { const __m256d tr = x7r;
          x7r = _mm256_fnmadd_pd(x7i,w7i,_mm256_mul_pd(tr,w7r));
          x7i = _mm256_fmadd_pd(tr,w7i,_mm256_mul_pd(x7i,w7r)); }

        /* DFT-4 of evens: x0, x2, x4, x6 */
        const __m256d epr = _mm256_add_pd(x0r,x4r), eqr = _mm256_sub_pd(x0r,x4r);
        const __m256d epi = _mm256_add_pd(x0i,x4i), eqi = _mm256_sub_pd(x0i,x4i);
        const __m256d err = _mm256_add_pd(x2r,x6r), esr = _mm256_sub_pd(x2r,x6r);
        const __m256d eri = _mm256_add_pd(x2i,x6i), esi = _mm256_sub_pd(x2i,x6i);
        const __m256d A0r = _mm256_add_pd(epr,err), A0i = _mm256_add_pd(epi,eri);
        const __m256d A2r = _mm256_sub_pd(epr,err), A2i = _mm256_sub_pd(epi,eri);
        const __m256d A1r = _mm256_add_pd(eqr,esi), A1i = _mm256_sub_pd(eqi,esr);
        const __m256d A3r = _mm256_sub_pd(eqr,esi), A3i = _mm256_add_pd(eqi,esr);

        /* DFT-4 of odds: x1, x3, x5, x7 */
        const __m256d opr = _mm256_add_pd(x1r,x5r), oqr = _mm256_sub_pd(x1r,x5r);
        const __m256d opi = _mm256_add_pd(x1i,x5i), oqi = _mm256_sub_pd(x1i,x5i);
        const __m256d orr = _mm256_add_pd(x3r,x7r), osr = _mm256_sub_pd(x3r,x7r);
        const __m256d ori = _mm256_add_pd(x3i,x7i), osi = _mm256_sub_pd(x3i,x7i);
        const __m256d B0r = _mm256_add_pd(opr,orr), B0i = _mm256_add_pd(opi,ori);
        const __m256d B2r = _mm256_sub_pd(opr,orr), B2i = _mm256_sub_pd(opi,ori);
        const __m256d B1r = _mm256_add_pd(oqr,osi), B1i = _mm256_sub_pd(oqi,osr);
        const __m256d B3r = _mm256_sub_pd(oqr,osi), B3i = _mm256_add_pd(oqi,osr);

        /* W8 combine and output */
        ST(&out_re[0*K+k], _mm256_add_pd(A0r,B0r)); ST(&out_im[0*K+k], _mm256_add_pd(A0i,B0i));
        ST(&out_re[4*K+k], _mm256_sub_pd(A0r,B0r)); ST(&out_im[4*K+k], _mm256_sub_pd(A0i,B0i));
        const __m256d t1r = _mm256_mul_pd(vc, _mm256_add_pd(B1r,B1i));
        const __m256d t1i = _mm256_mul_pd(vc, _mm256_sub_pd(B1i,B1r));
        ST(&out_re[1*K+k], _mm256_add_pd(A1r,t1r)); ST(&out_im[1*K+k], _mm256_add_pd(A1i,t1i));
        ST(&out_re[5*K+k], _mm256_sub_pd(A1r,t1r)); ST(&out_im[5*K+k], _mm256_sub_pd(A1i,t1i));
        ST(&out_re[2*K+k], _mm256_add_pd(A2r,B2i)); ST(&out_im[2*K+k], _mm256_sub_pd(A2i,B2r));
        ST(&out_re[6*K+k], _mm256_sub_pd(A2r,B2i)); ST(&out_im[6*K+k], _mm256_add_pd(A2i,B2r));
        const __m256d t3r = _mm256_mul_pd(vnc, _mm256_sub_pd(B3r,B3i));
        const __m256d t3i = _mm256_mul_pd(vnc, _mm256_add_pd(B3r,B3i));
        ST(&out_re[3*K+k], _mm256_add_pd(A3r,t3r)); ST(&out_im[3*K+k], _mm256_add_pd(A3i,t3i));
        ST(&out_re[7*K+k], _mm256_sub_pd(A3r,t3r)); ST(&out_im[7*K+k], _mm256_sub_pd(A3i,t3i));
    }
}

__attribute__((target("avx2,fma")))
static inline void
radix8_tw_log3_dit_kernel_bwd_avx2(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    const double * __restrict__ base_tw_re, const double * __restrict__ base_tw_im,
    size_t K)
{
    const __m256d vc = _mm256_set1_pd(0.707106781186547572737310929369414225220680236816);
    const __m256d vnc = _mm256_set1_pd(-0.707106781186547572737310929369414225220680236816);

    for (size_t k = 0; k < K; k += VL) {
        /* Log3: load W^1, W^2, W^4 bases */
        const __m256d b1r = LD(&base_tw_re[0*K+k]), b1i = LD(&base_tw_im[0*K+k]);
        const __m256d b2r = LD(&base_tw_re[1*K+k]), b2i = LD(&base_tw_im[1*K+k]);
        const __m256d b4r = LD(&base_tw_re[2*K+k]), b4i = LD(&base_tw_im[2*K+k]);

        /* Derive W^3 = W^1 * W^2 */
        const __m256d w3r = _mm256_fnmadd_pd(b1i,b2i,_mm256_mul_pd(b1r,b2r));
        const __m256d w3i = _mm256_fmadd_pd(b1r,b2i,_mm256_mul_pd(b1i,b2r));
        /* Derive W^5 = W^1 * W^4 */
        const __m256d w5r = _mm256_fnmadd_pd(b1i,b4i,_mm256_mul_pd(b1r,b4r));
        const __m256d w5i = _mm256_fmadd_pd(b1r,b4i,_mm256_mul_pd(b1i,b4r));
        /* Derive W^6 = W^2 * W^4 */
        const __m256d w6r = _mm256_fnmadd_pd(b2i,b4i,_mm256_mul_pd(b2r,b4r));
        const __m256d w6i = _mm256_fmadd_pd(b2r,b4i,_mm256_mul_pd(b2i,b4r));
        /* Derive W^7 = W^3 * W^4 */
        const __m256d w7r = _mm256_fnmadd_pd(w3i,b4i,_mm256_mul_pd(w3r,b4r));
        const __m256d w7i = _mm256_fmadd_pd(w3r,b4i,_mm256_mul_pd(w3i,b4r));

        const __m256d x0r = LD(&in_re[k]), x0i = LD(&in_im[k]);
        __m256d x1r = LD(&in_re[1*K+k]), x1i = LD(&in_im[1*K+k]);
        { const __m256d tr = x1r;
          x1r = _mm256_fmadd_pd(x1i,b1i,_mm256_mul_pd(tr,b1r));
          x1i = _mm256_fnmadd_pd(tr,b1i,_mm256_mul_pd(x1i,b1r)); }
        __m256d x2r = LD(&in_re[2*K+k]), x2i = LD(&in_im[2*K+k]);
        { const __m256d tr = x2r;
          x2r = _mm256_fmadd_pd(x2i,b2i,_mm256_mul_pd(tr,b2r));
          x2i = _mm256_fnmadd_pd(tr,b2i,_mm256_mul_pd(x2i,b2r)); }
        __m256d x3r = LD(&in_re[3*K+k]), x3i = LD(&in_im[3*K+k]);
        { const __m256d tr = x3r;
          x3r = _mm256_fmadd_pd(x3i,w3i,_mm256_mul_pd(tr,w3r));
          x3i = _mm256_fnmadd_pd(tr,w3i,_mm256_mul_pd(x3i,w3r)); }
        __m256d x4r = LD(&in_re[4*K+k]), x4i = LD(&in_im[4*K+k]);
        { const __m256d tr = x4r;
          x4r = _mm256_fmadd_pd(x4i,b4i,_mm256_mul_pd(tr,b4r));
          x4i = _mm256_fnmadd_pd(tr,b4i,_mm256_mul_pd(x4i,b4r)); }
        __m256d x5r = LD(&in_re[5*K+k]), x5i = LD(&in_im[5*K+k]);
        { const __m256d tr = x5r;
          x5r = _mm256_fmadd_pd(x5i,w5i,_mm256_mul_pd(tr,w5r));
          x5i = _mm256_fnmadd_pd(tr,w5i,_mm256_mul_pd(x5i,w5r)); }
        __m256d x6r = LD(&in_re[6*K+k]), x6i = LD(&in_im[6*K+k]);
        { const __m256d tr = x6r;
          x6r = _mm256_fmadd_pd(x6i,w6i,_mm256_mul_pd(tr,w6r));
          x6i = _mm256_fnmadd_pd(tr,w6i,_mm256_mul_pd(x6i,w6r)); }
        __m256d x7r = LD(&in_re[7*K+k]), x7i = LD(&in_im[7*K+k]);
        { const __m256d tr = x7r;
          x7r = _mm256_fmadd_pd(x7i,w7i,_mm256_mul_pd(tr,w7r));
          x7i = _mm256_fnmadd_pd(tr,w7i,_mm256_mul_pd(x7i,w7r)); }

        /* DFT-4 of evens: x0, x2, x4, x6 */
        const __m256d epr = _mm256_add_pd(x0r,x4r), eqr = _mm256_sub_pd(x0r,x4r);
        const __m256d epi = _mm256_add_pd(x0i,x4i), eqi = _mm256_sub_pd(x0i,x4i);
        const __m256d err = _mm256_add_pd(x2r,x6r), esr = _mm256_sub_pd(x2r,x6r);
        const __m256d eri = _mm256_add_pd(x2i,x6i), esi = _mm256_sub_pd(x2i,x6i);
        const __m256d A0r = _mm256_add_pd(epr,err), A0i = _mm256_add_pd(epi,eri);
        const __m256d A2r = _mm256_sub_pd(epr,err), A2i = _mm256_sub_pd(epi,eri);
        const __m256d A1r = _mm256_sub_pd(eqr,esi), A1i = _mm256_add_pd(eqi,esr);
        const __m256d A3r = _mm256_add_pd(eqr,esi), A3i = _mm256_sub_pd(eqi,esr);

        /* DFT-4 of odds: x1, x3, x5, x7 */
        const __m256d opr = _mm256_add_pd(x1r,x5r), oqr = _mm256_sub_pd(x1r,x5r);
        const __m256d opi = _mm256_add_pd(x1i,x5i), oqi = _mm256_sub_pd(x1i,x5i);
        const __m256d orr = _mm256_add_pd(x3r,x7r), osr = _mm256_sub_pd(x3r,x7r);
        const __m256d ori = _mm256_add_pd(x3i,x7i), osi = _mm256_sub_pd(x3i,x7i);
        const __m256d B0r = _mm256_add_pd(opr,orr), B0i = _mm256_add_pd(opi,ori);
        const __m256d B2r = _mm256_sub_pd(opr,orr), B2i = _mm256_sub_pd(opi,ori);
        const __m256d B1r = _mm256_sub_pd(oqr,osi), B1i = _mm256_add_pd(oqi,osr);
        const __m256d B3r = _mm256_add_pd(oqr,osi), B3i = _mm256_sub_pd(oqi,osr);

        /* W8 combine and output */
        ST(&out_re[0*K+k], _mm256_add_pd(A0r,B0r)); ST(&out_im[0*K+k], _mm256_add_pd(A0i,B0i));
        ST(&out_re[4*K+k], _mm256_sub_pd(A0r,B0r)); ST(&out_im[4*K+k], _mm256_sub_pd(A0i,B0i));
        const __m256d t1r = _mm256_mul_pd(vc, _mm256_sub_pd(B1r,B1i));
        const __m256d t1i = _mm256_mul_pd(vc, _mm256_add_pd(B1r,B1i));
        ST(&out_re[1*K+k], _mm256_add_pd(A1r,t1r)); ST(&out_im[1*K+k], _mm256_add_pd(A1i,t1i));
        ST(&out_re[5*K+k], _mm256_sub_pd(A1r,t1r)); ST(&out_im[5*K+k], _mm256_sub_pd(A1i,t1i));
        ST(&out_re[2*K+k], _mm256_sub_pd(A2r,B2i)); ST(&out_im[2*K+k], _mm256_add_pd(A2i,B2r));
        ST(&out_re[6*K+k], _mm256_add_pd(A2r,B2i)); ST(&out_im[6*K+k], _mm256_sub_pd(A2i,B2r));
        const __m256d t3r = _mm256_mul_pd(vnc, _mm256_add_pd(B3r,B3i));
        const __m256d t3i = _mm256_mul_pd(vc, _mm256_sub_pd(B3r,B3i));
        ST(&out_re[3*K+k], _mm256_add_pd(A3r,t3r)); ST(&out_im[3*K+k], _mm256_add_pd(A3i,t3i));
        ST(&out_re[7*K+k], _mm256_sub_pd(A3r,t3r)); ST(&out_im[7*K+k], _mm256_sub_pd(A3i,t3i));
    }
}

__attribute__((target("avx2,fma")))
static inline void
radix8_tw_log3_dif_kernel_fwd_avx2(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    const double * __restrict__ base_tw_re, const double * __restrict__ base_tw_im,
    size_t K)
{
    const __m256d vc = _mm256_set1_pd(0.707106781186547572737310929369414225220680236816);
    const __m256d vnc = _mm256_set1_pd(-0.707106781186547572737310929369414225220680236816);

    for (size_t k = 0; k < K; k += VL) {
        /* Log3: load W^1, W^2, W^4 bases */
        const __m256d b1r = LD(&base_tw_re[0*K+k]), b1i = LD(&base_tw_im[0*K+k]);
        const __m256d b2r = LD(&base_tw_re[1*K+k]), b2i = LD(&base_tw_im[1*K+k]);
        const __m256d b4r = LD(&base_tw_re[2*K+k]), b4i = LD(&base_tw_im[2*K+k]);

        /* Derive W^3,5,6,7 */
        const __m256d w3r = _mm256_fnmadd_pd(b1i,b2i,_mm256_mul_pd(b1r,b2r));
        const __m256d w3i = _mm256_fmadd_pd(b1r,b2i,_mm256_mul_pd(b1i,b2r));
        const __m256d w5r = _mm256_fnmadd_pd(b1i,b4i,_mm256_mul_pd(b1r,b4r));
        const __m256d w5i = _mm256_fmadd_pd(b1r,b4i,_mm256_mul_pd(b1i,b4r));
        const __m256d w6r = _mm256_fnmadd_pd(b2i,b4i,_mm256_mul_pd(b2r,b4r));
        const __m256d w6i = _mm256_fmadd_pd(b2r,b4i,_mm256_mul_pd(b2i,b4r));
        const __m256d w7r = _mm256_fnmadd_pd(w3i,b4i,_mm256_mul_pd(w3r,b4r));
        const __m256d w7i = _mm256_fmadd_pd(w3r,b4i,_mm256_mul_pd(w3i,b4r));

        const __m256d x0r = LD(&in_re[0*K+k]), x0i = LD(&in_im[0*K+k]);
        const __m256d x1r = LD(&in_re[1*K+k]), x1i = LD(&in_im[1*K+k]);
        const __m256d x2r = LD(&in_re[2*K+k]), x2i = LD(&in_im[2*K+k]);
        const __m256d x3r = LD(&in_re[3*K+k]), x3i = LD(&in_im[3*K+k]);
        const __m256d x4r = LD(&in_re[4*K+k]), x4i = LD(&in_im[4*K+k]);
        const __m256d x5r = LD(&in_re[5*K+k]), x5i = LD(&in_im[5*K+k]);
        const __m256d x6r = LD(&in_re[6*K+k]), x6i = LD(&in_im[6*K+k]);
        const __m256d x7r = LD(&in_re[7*K+k]), x7i = LD(&in_im[7*K+k]);
        const __m256d epr = _mm256_add_pd(x0r,x4r), eqr = _mm256_sub_pd(x0r,x4r);
        const __m256d epi = _mm256_add_pd(x0i,x4i), eqi = _mm256_sub_pd(x0i,x4i);
        const __m256d err = _mm256_add_pd(x2r,x6r), esr = _mm256_sub_pd(x2r,x6r);
        const __m256d eri = _mm256_add_pd(x2i,x6i), esi = _mm256_sub_pd(x2i,x6i);
        const __m256d A0r = _mm256_add_pd(epr,err), A0i = _mm256_add_pd(epi,eri);
        const __m256d A2r = _mm256_sub_pd(epr,err), A2i = _mm256_sub_pd(epi,eri);
        const __m256d A1r = _mm256_add_pd(eqr,esi), A1i = _mm256_sub_pd(eqi,esr);
        const __m256d A3r = _mm256_sub_pd(eqr,esi), A3i = _mm256_add_pd(eqi,esr);
        const __m256d opr = _mm256_add_pd(x1r,x5r), oqr = _mm256_sub_pd(x1r,x5r);
        const __m256d opi = _mm256_add_pd(x1i,x5i), oqi = _mm256_sub_pd(x1i,x5i);
        const __m256d orr = _mm256_add_pd(x3r,x7r), osr = _mm256_sub_pd(x3r,x7r);
        const __m256d ori = _mm256_add_pd(x3i,x7i), osi = _mm256_sub_pd(x3i,x7i);
        const __m256d B0r = _mm256_add_pd(opr,orr), B0i = _mm256_add_pd(opi,ori);
        const __m256d B2r = _mm256_sub_pd(opr,orr), B2i = _mm256_sub_pd(opi,ori);
        const __m256d B1r = _mm256_add_pd(oqr,osi), B1i = _mm256_sub_pd(oqi,osr);
        const __m256d B3r = _mm256_sub_pd(oqr,osi), B3i = _mm256_add_pd(oqi,osr);
        __m256d y0r = _mm256_add_pd(A0r,B0r), y0i = _mm256_add_pd(A0i,B0i);
        __m256d y4r = _mm256_sub_pd(A0r,B0r), y4i = _mm256_sub_pd(A0i,B0i);
        const __m256d wt1r = _mm256_mul_pd(vc, _mm256_add_pd(B1r,B1i)), wt1i = _mm256_mul_pd(vc, _mm256_sub_pd(B1i,B1r));
        __m256d y1r = _mm256_add_pd(A1r,wt1r), y1i = _mm256_add_pd(A1i,wt1i);
        __m256d y5r = _mm256_sub_pd(A1r,wt1r), y5i = _mm256_sub_pd(A1i,wt1i);
        __m256d y2r = _mm256_add_pd(A2r,B2i), y2i = _mm256_sub_pd(A2i,B2r);
        __m256d y6r = _mm256_sub_pd(A2r,B2i), y6i = _mm256_add_pd(A2i,B2r);
        const __m256d wt3r = _mm256_mul_pd(vnc, _mm256_sub_pd(B3r,B3i)), wt3i = _mm256_mul_pd(vnc, _mm256_add_pd(B3r,B3i));
        __m256d y3r = _mm256_add_pd(A3r,wt3r), y3i = _mm256_add_pd(A3i,wt3i);
        __m256d y7r = _mm256_sub_pd(A3r,wt3r), y7i = _mm256_sub_pd(A3i,wt3i);

        /* DIF: post-twiddle outputs 1..7 */
        ST(&out_re[0*K+k], y0r); ST(&out_im[0*K+k], y0i);
        { const __m256d tr = y1r;
          y1r = _mm256_fnmadd_pd(y1i,b1i,_mm256_mul_pd(tr,b1r));
          y1i = _mm256_fmadd_pd(tr,b1i,_mm256_mul_pd(y1i,b1r)); }
        ST(&out_re[1*K+k], y1r); ST(&out_im[1*K+k], y1i);
        { const __m256d tr = y2r;
          y2r = _mm256_fnmadd_pd(y2i,b2i,_mm256_mul_pd(tr,b2r));
          y2i = _mm256_fmadd_pd(tr,b2i,_mm256_mul_pd(y2i,b2r)); }
        ST(&out_re[2*K+k], y2r); ST(&out_im[2*K+k], y2i);
        { const __m256d tr = y3r;
          y3r = _mm256_fnmadd_pd(y3i,w3i,_mm256_mul_pd(tr,w3r));
          y3i = _mm256_fmadd_pd(tr,w3i,_mm256_mul_pd(y3i,w3r)); }
        ST(&out_re[3*K+k], y3r); ST(&out_im[3*K+k], y3i);
        { const __m256d tr = y4r;
          y4r = _mm256_fnmadd_pd(y4i,b4i,_mm256_mul_pd(tr,b4r));
          y4i = _mm256_fmadd_pd(tr,b4i,_mm256_mul_pd(y4i,b4r)); }
        ST(&out_re[4*K+k], y4r); ST(&out_im[4*K+k], y4i);
        { const __m256d tr = y5r;
          y5r = _mm256_fnmadd_pd(y5i,w5i,_mm256_mul_pd(tr,w5r));
          y5i = _mm256_fmadd_pd(tr,w5i,_mm256_mul_pd(y5i,w5r)); }
        ST(&out_re[5*K+k], y5r); ST(&out_im[5*K+k], y5i);
        { const __m256d tr = y6r;
          y6r = _mm256_fnmadd_pd(y6i,w6i,_mm256_mul_pd(tr,w6r));
          y6i = _mm256_fmadd_pd(tr,w6i,_mm256_mul_pd(y6i,w6r)); }
        ST(&out_re[6*K+k], y6r); ST(&out_im[6*K+k], y6i);
        { const __m256d tr = y7r;
          y7r = _mm256_fnmadd_pd(y7i,w7i,_mm256_mul_pd(tr,w7r));
          y7i = _mm256_fmadd_pd(tr,w7i,_mm256_mul_pd(y7i,w7r)); }
        ST(&out_re[7*K+k], y7r); ST(&out_im[7*K+k], y7i);
    }
}

__attribute__((target("avx2,fma")))
static inline void
radix8_tw_log3_dif_kernel_bwd_avx2(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    const double * __restrict__ base_tw_re, const double * __restrict__ base_tw_im,
    size_t K)
{
    const __m256d vc = _mm256_set1_pd(0.707106781186547572737310929369414225220680236816);
    const __m256d vnc = _mm256_set1_pd(-0.707106781186547572737310929369414225220680236816);

    for (size_t k = 0; k < K; k += VL) {
        /* Log3: load W^1, W^2, W^4 bases */
        const __m256d b1r = LD(&base_tw_re[0*K+k]), b1i = LD(&base_tw_im[0*K+k]);
        const __m256d b2r = LD(&base_tw_re[1*K+k]), b2i = LD(&base_tw_im[1*K+k]);
        const __m256d b4r = LD(&base_tw_re[2*K+k]), b4i = LD(&base_tw_im[2*K+k]);

        /* Derive W^3,5,6,7 */
        const __m256d w3r = _mm256_fnmadd_pd(b1i,b2i,_mm256_mul_pd(b1r,b2r));
        const __m256d w3i = _mm256_fmadd_pd(b1r,b2i,_mm256_mul_pd(b1i,b2r));
        const __m256d w5r = _mm256_fnmadd_pd(b1i,b4i,_mm256_mul_pd(b1r,b4r));
        const __m256d w5i = _mm256_fmadd_pd(b1r,b4i,_mm256_mul_pd(b1i,b4r));
        const __m256d w6r = _mm256_fnmadd_pd(b2i,b4i,_mm256_mul_pd(b2r,b4r));
        const __m256d w6i = _mm256_fmadd_pd(b2r,b4i,_mm256_mul_pd(b2i,b4r));
        const __m256d w7r = _mm256_fnmadd_pd(w3i,b4i,_mm256_mul_pd(w3r,b4r));
        const __m256d w7i = _mm256_fmadd_pd(w3r,b4i,_mm256_mul_pd(w3i,b4r));

        const __m256d x0r = LD(&in_re[0*K+k]), x0i = LD(&in_im[0*K+k]);
        const __m256d x1r = LD(&in_re[1*K+k]), x1i = LD(&in_im[1*K+k]);
        const __m256d x2r = LD(&in_re[2*K+k]), x2i = LD(&in_im[2*K+k]);
        const __m256d x3r = LD(&in_re[3*K+k]), x3i = LD(&in_im[3*K+k]);
        const __m256d x4r = LD(&in_re[4*K+k]), x4i = LD(&in_im[4*K+k]);
        const __m256d x5r = LD(&in_re[5*K+k]), x5i = LD(&in_im[5*K+k]);
        const __m256d x6r = LD(&in_re[6*K+k]), x6i = LD(&in_im[6*K+k]);
        const __m256d x7r = LD(&in_re[7*K+k]), x7i = LD(&in_im[7*K+k]);
        const __m256d epr = _mm256_add_pd(x0r,x4r), eqr = _mm256_sub_pd(x0r,x4r);
        const __m256d epi = _mm256_add_pd(x0i,x4i), eqi = _mm256_sub_pd(x0i,x4i);
        const __m256d err = _mm256_add_pd(x2r,x6r), esr = _mm256_sub_pd(x2r,x6r);
        const __m256d eri = _mm256_add_pd(x2i,x6i), esi = _mm256_sub_pd(x2i,x6i);
        const __m256d A0r = _mm256_add_pd(epr,err), A0i = _mm256_add_pd(epi,eri);
        const __m256d A2r = _mm256_sub_pd(epr,err), A2i = _mm256_sub_pd(epi,eri);
        const __m256d A1r = _mm256_sub_pd(eqr,esi), A1i = _mm256_add_pd(eqi,esr);
        const __m256d A3r = _mm256_add_pd(eqr,esi), A3i = _mm256_sub_pd(eqi,esr);
        const __m256d opr = _mm256_add_pd(x1r,x5r), oqr = _mm256_sub_pd(x1r,x5r);
        const __m256d opi = _mm256_add_pd(x1i,x5i), oqi = _mm256_sub_pd(x1i,x5i);
        const __m256d orr = _mm256_add_pd(x3r,x7r), osr = _mm256_sub_pd(x3r,x7r);
        const __m256d ori = _mm256_add_pd(x3i,x7i), osi = _mm256_sub_pd(x3i,x7i);
        const __m256d B0r = _mm256_add_pd(opr,orr), B0i = _mm256_add_pd(opi,ori);
        const __m256d B2r = _mm256_sub_pd(opr,orr), B2i = _mm256_sub_pd(opi,ori);
        const __m256d B1r = _mm256_sub_pd(oqr,osi), B1i = _mm256_add_pd(oqi,osr);
        const __m256d B3r = _mm256_add_pd(oqr,osi), B3i = _mm256_sub_pd(oqi,osr);
        __m256d y0r = _mm256_add_pd(A0r,B0r), y0i = _mm256_add_pd(A0i,B0i);
        __m256d y4r = _mm256_sub_pd(A0r,B0r), y4i = _mm256_sub_pd(A0i,B0i);
        const __m256d wt1r = _mm256_mul_pd(vc, _mm256_sub_pd(B1r,B1i)), wt1i = _mm256_mul_pd(vc, _mm256_add_pd(B1r,B1i));
        __m256d y1r = _mm256_add_pd(A1r,wt1r), y1i = _mm256_add_pd(A1i,wt1i);
        __m256d y5r = _mm256_sub_pd(A1r,wt1r), y5i = _mm256_sub_pd(A1i,wt1i);
        __m256d y2r = _mm256_sub_pd(A2r,B2i), y2i = _mm256_add_pd(A2i,B2r);
        __m256d y6r = _mm256_add_pd(A2r,B2i), y6i = _mm256_sub_pd(A2i,B2r);
        const __m256d wt3r = _mm256_mul_pd(vnc, _mm256_add_pd(B3r,B3i)), wt3i = _mm256_mul_pd(vc, _mm256_sub_pd(B3r,B3i));
        __m256d y3r = _mm256_add_pd(A3r,wt3r), y3i = _mm256_add_pd(A3i,wt3i);
        __m256d y7r = _mm256_sub_pd(A3r,wt3r), y7i = _mm256_sub_pd(A3i,wt3i);

        /* DIF: post-twiddle outputs 1..7 */
        ST(&out_re[0*K+k], y0r); ST(&out_im[0*K+k], y0i);
        { const __m256d tr = y1r;
          y1r = _mm256_fmadd_pd(y1i,b1i,_mm256_mul_pd(tr,b1r));
          y1i = _mm256_fnmadd_pd(tr,b1i,_mm256_mul_pd(y1i,b1r)); }
        ST(&out_re[1*K+k], y1r); ST(&out_im[1*K+k], y1i);
        { const __m256d tr = y2r;
          y2r = _mm256_fmadd_pd(y2i,b2i,_mm256_mul_pd(tr,b2r));
          y2i = _mm256_fnmadd_pd(tr,b2i,_mm256_mul_pd(y2i,b2r)); }
        ST(&out_re[2*K+k], y2r); ST(&out_im[2*K+k], y2i);
        { const __m256d tr = y3r;
          y3r = _mm256_fmadd_pd(y3i,w3i,_mm256_mul_pd(tr,w3r));
          y3i = _mm256_fnmadd_pd(tr,w3i,_mm256_mul_pd(y3i,w3r)); }
        ST(&out_re[3*K+k], y3r); ST(&out_im[3*K+k], y3i);
        { const __m256d tr = y4r;
          y4r = _mm256_fmadd_pd(y4i,b4i,_mm256_mul_pd(tr,b4r));
          y4i = _mm256_fnmadd_pd(tr,b4i,_mm256_mul_pd(y4i,b4r)); }
        ST(&out_re[4*K+k], y4r); ST(&out_im[4*K+k], y4i);
        { const __m256d tr = y5r;
          y5r = _mm256_fmadd_pd(y5i,w5i,_mm256_mul_pd(tr,w5r));
          y5i = _mm256_fnmadd_pd(tr,w5i,_mm256_mul_pd(y5i,w5r)); }
        ST(&out_re[5*K+k], y5r); ST(&out_im[5*K+k], y5i);
        { const __m256d tr = y6r;
          y6r = _mm256_fmadd_pd(y6i,w6i,_mm256_mul_pd(tr,w6r));
          y6i = _mm256_fnmadd_pd(tr,w6i,_mm256_mul_pd(y6i,w6r)); }
        ST(&out_re[6*K+k], y6r); ST(&out_im[6*K+k], y6i);
        { const __m256d tr = y7r;
          y7r = _mm256_fmadd_pd(y7i,w7i,_mm256_mul_pd(tr,w7r));
          y7i = _mm256_fnmadd_pd(tr,w7i,_mm256_mul_pd(y7i,w7r)); }
        ST(&out_re[7*K+k], y7r); ST(&out_im[7*K+k], y7i);
    }
}

