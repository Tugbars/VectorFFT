static inline void
radix8_n1_fwd(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    size_t is, size_t os, size_t vl, size_t ivs, size_t ovs)
{
    const double c = 0.707106781186547572737310929369414225220680236816;
    for (size_t k = 0; k < vl; k++) {
        const double x0r = in_re[0*is + k*ivs], x0i = in_im[0*is + k*ivs];
        const double x1r = in_re[1*is + k*ivs], x1i = in_im[1*is + k*ivs];
        const double x2r = in_re[2*is + k*ivs], x2i = in_im[2*is + k*ivs];
        const double x3r = in_re[3*is + k*ivs], x3i = in_im[3*is + k*ivs];
        const double x4r = in_re[4*is + k*ivs], x4i = in_im[4*is + k*ivs];
        const double x5r = in_re[5*is + k*ivs], x5i = in_im[5*is + k*ivs];
        const double x6r = in_re[6*is + k*ivs], x6i = in_im[6*is + k*ivs];
        const double x7r = in_re[7*is + k*ivs], x7i = in_im[7*is + k*ivs];
        const double ep_r=x0r+x4r,eq_r=x0r-x4r,er_r=x2r+x6r,es_r=x2r-x6r;
        const double ep_i=x0i+x4i,eq_i=x0i-x4i,er_i=x2i+x6i,es_i=x2i-x6i;
        const double A0r=ep_r+er_r,A0i=ep_i+er_i,A2r=ep_r-er_r,A2i=ep_i-er_i;
        const double A1r=eq_r+es_i,A1i=eq_i-es_r,A3r=eq_r-es_i,A3i=eq_i+es_r;
        const double op_r=x1r+x5r,oq_r=x1r-x5r,or_r=x3r+x7r,os_r=x3r-x7r;
        const double op_i=x1i+x5i,oq_i=x1i-x5i,or_i=x3i+x7i,os_i=x3i-x7i;
        const double B0r=op_r+or_r,B0i=op_i+or_i,B2r=op_r-or_r,B2i=op_i-or_i;
        const double B1r=oq_r+os_i,B1i=oq_i-os_r,B3r=oq_r-os_i,B3i=oq_i+os_r;
        out_re[0*os+k*ovs]=A0r+B0r; out_im[0*os+k*ovs]=A0i+B0i;
        out_re[4*os+k*ovs]=A0r-B0r; out_im[4*os+k*ovs]=A0i-B0i;
        {double w1r=c*(B1r+B1i),w1i=c*(B1i-B1r);
        out_re[1*os+k*ovs]=A1r+w1r; out_im[1*os+k*ovs]=A1i+w1i;
        out_re[5*os+k*ovs]=A1r-w1r; out_im[5*os+k*ovs]=A1i-w1i;}
        out_re[2*os+k*ovs]=A2r+B2i; out_im[2*os+k*ovs]=A2i-B2r;
        out_re[6*os+k*ovs]=A2r-B2i; out_im[6*os+k*ovs]=A2i+B2r;
        {double w3r=-c*(B3r-B3i),w3i=-c*(B3r+B3i);
        out_re[3*os+k*ovs]=A3r+w3r; out_im[3*os+k*ovs]=A3i+w3i;
        out_re[7*os+k*ovs]=A3r-w3r; out_im[7*os+k*ovs]=A3i-w3i;}
    }
}

