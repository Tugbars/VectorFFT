(* emit_trig_registry.ml — auto-generate the trig (real-to-real) registry.
 *
 * Sibling to emit_rfft_registry.ml / emit_oop_registry.ml. Walks
 * Vfft_v2.Coverage.files "trig-<isa>" and emits a registrar filling
 * trig_codelets_t. One uniform 3-arg ABI for the whole family, so every
 * slot is the same fn type; the kind selects WHICH slot, the size is the
 * radix index.
 *
 * Symbol == filename stem: radix{N}_{kind}_{isa}  (the trig generator emits
 * the symbol identical to the file basename, verified).
 *
 * Usage:
 *   dune exec bin/emit_trig_registry.exe -- --isa avx512 > generated/trig_registry_avx512.h
 *   dune exec bin/emit_trig_registry.exe -- --isa avx2   > generated/trig_registry_avx2.h
 *)

let kinds = ["dct1";"dct2";"dct3";"dct4";"dst1";"dst2";"dst3";"dst4";"dht"]

let contains hay needle =
  let lh = String.length hay and ln = String.length needle in
  let rec go i = i + ln <= lh && (String.sub hay i ln = needle || go (i+1)) in
  ln > 0 && go 0

(* kind token in the filename, e.g. "_dct2_" -> dct2. Test dct1/dst1 etc
 * by exact "_<kind>_" so dct2 doesn't match inside another token. *)
let kind_of stem =
  let rec find = function
    | [] -> None
    | k :: tl -> if contains stem ("_" ^ k ^ "_") then Some k else find tl
  in find kinds

let radix_of stem =
  if String.length stem > 5 && String.sub stem 0 5 = "radix" then
    let rec d i acc =
      if i < String.length stem && stem.[i] >= '0' && stem.[i] <= '9'
      then d (i+1) (acc*10 + (Char.code stem.[i] - Char.code '0'))
      else if i = 5 then None else Some acc
    in d 5 0
  else None

let () =
  let isa = ref "avx512" in
  Arg.parse [ ("--isa", Arg.Set_string isa, "avx2|avx512") ]
    (fun _ -> ()) "emit_trig_registry --isa <avx2|avx512>";
  let isa = !isa in
  let quadrant = "trig-" ^ isa in
  let files = Vfft_v2.Coverage.files quadrant in

  (* (symbol, slot=kind, radix) *)
  let entries =
    List.filter_map (fun (fname_c, _argv) ->
      let stem =
        if Filename.check_suffix fname_c ".c"
        then Filename.chop_suffix fname_c ".c" else fname_c in
      match radix_of stem, kind_of stem with
      | Some r, Some k -> Some (stem, k, r)   (* symbol == stem *)
      | _ -> None
    ) files
  in

  Printf.printf "/* AUTO-GENERATED by bin/emit_trig_registry.ml from\n";
  Printf.printf " * Coverage.files \"%s\". DO NOT EDIT BY HAND.\n" quadrant;
  Printf.printf " * Regenerate: dune build (promote rule) or\n";
  Printf.printf " *   dune exec bin/emit_trig_registry.exe -- --isa %s\n" isa;
  Printf.printf " *\n";
  Printf.printf " * Uniform 3-arg real-to-real ABI; kind selects the slot, N is the\n";
  Printf.printf " * index. Coverage-driven: a new trig codelet auto-appears here. */\n";
  Printf.printf "#ifndef VFFT_TRIG_REGISTRY_%s_H\n" (String.uppercase_ascii isa);
  Printf.printf "#define VFFT_TRIG_REGISTRY_%s_H\n\n" (String.uppercase_ascii isa);
  Printf.printf "#include \"trig_codelets.h\"\n\n";

  Printf.printf "/* extern declarations (every trig codelet in coverage) */\n";
  List.iter (fun (sym, _slot, _r) ->
    Printf.printf "extern void %s(const double*, double*, size_t);\n" sym
  ) entries;
  Printf.printf "\n";

  Printf.printf "static inline void trig_register_all_%s(trig_codelets_t *reg)\n{\n" isa;
  List.iter (fun (sym, slot, r) ->
    Printf.printf "    reg->%s[%d] = %s;\n" slot r sym
  ) entries;
  Printf.printf "}\n\n";
  Printf.printf "#endif\n"
