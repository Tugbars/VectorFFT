(* emit_oop_registry.ml — auto-generate the OOP codelet registry.
 *
 * Sibling to emit_rfft_registry.ml. Walks Vfft_v2.Coverage.files "oop-<isa>"
 * (the single source of truth) and emits an ABI-typed registrar that fills
 * oop_codelets_t. Adding an OOP codelet to coverage auto-populates a slot.
 *
 * Two ABIs in this family (the reason spec gets its own slots):
 *   regular -> vfft_oop11_fn (11-arg, runtime strides)
 *   spec    -> vfft_oop7_fn  (7-arg, baked strides, rv = r*8)
 * Three kinds: n1 (leaf), t1p (flat twiddle, default), t1p_log3 (port-
 * rebalance opt-in). flat vs log3 SELECTION is a planner/cost-model job
 * (load-bound -> log3); the registry only makes both REACHABLE.
 *
 * Symbol conventions (verified from the emitted tree):
 *   n1            radix{R}_n1_oop_fwd_{isa}_UG_UG
 *   t1p flat      radix{R}_t1p_oop_fwd_{isa}_UG_UG
 *   t1p log3      radix{R}_t1p_oop_fwd_{isa}_UG_UG_log3
 *   + spec        ...append _spec  (and ABI drops to 7-arg)
 * Filenames differ in token ORDER (t1p_log3_oop..., ..._spec) so we classify
 * from the filename but emit the SYMBOL via the rule above.
 *
 * Usage:
 *   dune exec bin/emit_oop_registry.exe -- --isa avx512 > generated/oop_registry_avx512.h
 *   dune exec bin/emit_oop_registry.exe -- --isa avx2   > generated/oop_registry_avx2.h
 *)

type kind = N1 | T1p | T1p_log3 | Unknown
type abi = Reg | Spec

let contains hay needle =
  let lh = String.length hay and ln = String.length needle in
  let rec go i = i + ln <= lh && (String.sub hay i ln = needle || go (i + 1)) in
  ln > 0 && go 0

(* classify from the filename stem (no .c). Order: log3 before plain t1p. *)
let kind_of stem =
  if contains stem "_t1p_log3_oop" then T1p_log3
  else if contains stem "_t1p_oop" then T1p
  else if contains stem "_n1_oop" then N1
  else Unknown

let abi_of stem = if contains stem "_spec" then Spec else Reg

let radix_of stem =
  if String.length stem > 5 && String.sub stem 0 5 = "radix" then
    let rec d i acc =
      if i < String.length stem && stem.[i] >= '0' && stem.[i] <= '9' then
        d (i + 1) ((acc * 10) + (Char.code stem.[i] - Char.code '0'))
      else if i = 5 then None
      else Some acc
    in
    d 5 0
  else None

(* the SYMBOL a codelet of (kind, abi, radix, isa) emits. *)
let symbol_of kind abi r isa =
  let base =
    match kind with
    | N1 -> Printf.sprintf "radix%d_n1_oop_fwd_%s_UG_UG" r isa
    | T1p -> Printf.sprintf "radix%d_t1p_oop_fwd_%s_UG_UG" r isa
    | T1p_log3 -> Printf.sprintf "radix%d_t1p_oop_fwd_%s_UG_UG_log3" r isa
    | Unknown -> ""
  in
  match abi with Spec -> base ^ "_spec" | Reg -> base

(* slot field in oop_codelets_t for (kind, abi). None for Unknown. *)
let slot_of kind abi =
  match (kind, abi) with
  | N1, Reg -> Some "n1"
  | T1p, Reg -> Some "t1p"
  | T1p_log3, Reg -> Some "t1p_log3"
  | N1, Spec -> Some "n1_spec"
  | T1p, Spec -> Some "t1p_spec"
  | T1p_log3, Spec -> Some "t1p_log3_spec"
  | Unknown, _ -> None

let is_spec_slot slot = contains slot "spec"

let () =
  let isa = ref "avx512" in
  Arg.parse
    [ ("--isa", Arg.Set_string isa, "avx2|avx512") ]
    (fun _ -> ())
    "emit_oop_registry --isa <avx2|avx512>";
  let isa = !isa in
  let quadrant = "oop-" ^ isa in
  let files = Vfft_v2.Coverage.files quadrant in

  (* (symbol, slot, radix), skipping unclassifiable entries *)
  let entries =
    List.filter_map
      (fun (fname_c, _argv) ->
        let stem =
          if Filename.check_suffix fname_c ".c" then
            Filename.chop_suffix fname_c ".c"
          else fname_c
        in
        let k = kind_of stem and a = abi_of stem in
        match (radix_of stem, slot_of k a) with
        | Some r, Some slot -> Some (symbol_of k a r isa, slot, r)
        | _ -> None)
      files
  in

  Printf.printf "/* AUTO-GENERATED by bin/emit_oop_registry.ml from\n";
  Printf.printf " * Coverage.files \"%s\". DO NOT EDIT BY HAND.\n" quadrant;
  Printf.printf " * Regenerate: dune build (promote rule) or\n";
  Printf.printf " *   dune exec bin/emit_oop_registry.exe -- --isa %s\n" isa;
  Printf.printf " *\n";
  Printf.printf
    " * ABI-typed: regular -> 11-arg slots, spec -> 7-arg slots. A wrong-ABI\n";
  Printf.printf
    " * wire is a compile error. flat vs log3 selection is a planner job;\n";
  Printf.printf " * this only makes both reachable. */\n";
  Printf.printf "#ifndef VFFT_OOP_REGISTRY_%s_H\n" (String.uppercase_ascii isa);
  Printf.printf "#define VFFT_OOP_REGISTRY_%s_H\n\n"
    (String.uppercase_ascii isa);
  Printf.printf "#include \"oop_codelets.h\"\n\n";

  (* externs: 11-arg for regular slots, 7-arg for spec slots *)
  let proto slot sym =
    if is_spec_slot slot then
      Printf.sprintf
        "extern void %s(const double*, const double*, double*, double*, const \
         double*, const double*, size_t);"
        sym
    else
      Printf.sprintf
        "extern void %s(const double*, const double*, double*, double*, const \
         double*, const double*, size_t, size_t, size_t, size_t, size_t);"
        sym
  in
  Printf.printf "/* extern declarations (every OOP codelet in coverage) */\n";
  List.iter (fun (sym, slot, _r) -> print_endline (proto slot sym)) entries;
  Printf.printf "\n";

  Printf.printf
    "static inline void oop_register_all_%s(oop_codelets_t *reg)\n{\n" isa;
  List.iter
    (fun (sym, slot, r) -> Printf.printf "    reg->%s[%d] = %s;\n" slot r sym)
    entries;
  Printf.printf "}\n\n";
  Printf.printf "#endif\n"
