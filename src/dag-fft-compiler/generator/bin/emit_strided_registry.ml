(* emit_strided_registry.ml — auto-generate the strided (Design C 2D) registry.
 *
 * Sibling emitter. Walks Vfft_v2.Coverage.files "strided-<isa>" and fills
 * strided_codelets_t. One uniform 6-arg in-place ABI; direction (fwd/bwd)
 * selects the slot, radix is the index.
 *
 * NOTE filename != symbol for this family:
 *   filename : r{R}_n1_{fwd,bwd}_strided.c
 *   symbol   : radix{R}_n1_{fwd,bwd}_{isa}_strided
 * so we parse R and dir from the filename and rebuild the symbol.
 *
 * Usage:
 *   dune exec bin/emit_strided_registry.exe -- --isa avx512 > generated/strided_registry_avx512.h
 *   dune exec bin/emit_strided_registry.exe -- --isa avx2   > generated/strided_registry_avx2.h
 *)

let contains hay needle =
  let lh = String.length hay and ln = String.length needle in
  let rec go i = i + ln <= lh && (String.sub hay i ln = needle || go (i + 1)) in
  ln > 0 && go 0

(* radix from the "r{R}_" prefix of the strided filename stem *)
let radix_of stem =
  if String.length stem > 1 && stem.[0] = 'r' then
    let rec d i acc =
      if i < String.length stem && stem.[i] >= '0' && stem.[i] <= '9' then
        d (i + 1) ((acc * 10) + (Char.code stem.[i] - Char.code '0'))
      else if i = 1 then None
      else Some acc
    in
    d 1 0
  else None

let dir_of stem =
  if contains stem "_fwd_" then Some "fwd"
  else if contains stem "_bwd_" then Some "bwd"
  else None

let () =
  let isa = ref "avx512" in
  Arg.parse
    [ ("--isa", Arg.Set_string isa, "avx2|avx512") ]
    (fun _ -> ())
    "emit_strided_registry --isa <avx2|avx512>";
  let isa = !isa in
  let quadrant = "strided-" ^ isa in
  let files = Vfft_v2.Coverage.files quadrant in

  (* (symbol, slot, radix) *)
  let entries =
    List.filter_map
      (fun (fname_c, _argv) ->
        let stem =
          if Filename.check_suffix fname_c ".c" then
            Filename.chop_suffix fname_c ".c"
          else fname_c
        in
        match (radix_of stem, dir_of stem) with
        | Some r, Some dir ->
            let sym = Printf.sprintf "radix%d_n1_%s_%s_strided" r dir isa in
            let slot = "n1_" ^ dir in
            Some (sym, slot, r)
        | _ -> None)
      files
  in

  Printf.printf "/* AUTO-GENERATED by bin/emit_strided_registry.ml from\n";
  Printf.printf " * Coverage.files \"%s\". DO NOT EDIT BY HAND.\n" quadrant;
  Printf.printf " * Regenerate: dune build (promote rule) or\n";
  Printf.printf " *   dune exec bin/emit_strided_registry.exe -- --isa %s\n" isa;
  Printf.printf " *\n";
  Printf.printf
    " * Uniform 6-arg in-place 2D ABI; direction selects the slot, R is the\n";
  Printf.printf
    " * index. Radix sets differ per ISA (avx2 spills past R=256); slots for\n";
  Printf.printf " * radices not generated on this ISA stay NULL. */\n";
  Printf.printf "#ifndef VFFT_STRIDED_REGISTRY_%s_H\n"
    (String.uppercase_ascii isa);
  Printf.printf "#define VFFT_STRIDED_REGISTRY_%s_H\n\n"
    (String.uppercase_ascii isa);
  Printf.printf "#include \"strided_codelets.h\"\n\n";

  Printf.printf
    "/* extern declarations (every strided codelet in coverage) */\n";
  List.iter
    (fun (sym, _slot, _r) ->
      Printf.printf
        "extern void %s(double*, double*, const double*, const double*, \
         size_t, size_t);\n"
        sym)
    entries;
  Printf.printf "\n";

  Printf.printf
    "static inline void strided_register_all_%s(strided_codelets_t *reg)\n{\n"
    isa;
  List.iter
    (fun (sym, slot, r) -> Printf.printf "    reg->%s[%d] = %s;\n" slot r sym)
    entries;
  Printf.printf "}\n\n";
  Printf.printf "#endif\n"
