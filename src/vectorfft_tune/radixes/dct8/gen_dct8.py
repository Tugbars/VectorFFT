#!/usr/bin/env python3
"""
gen_dct8.py — Specialized DCT-II N=8 codelet (FFTW REDFT10 convention).

Output: a single straight-line C function that computes Y[k] for k=0..7,
batched across the K dimension using SIMD (AVX2 default; scalar tail).
Convention: Y[k] = 2 * sum_{n=0..7} x[n] * cos(π k (2n+1) / 16).

Algorithm — factored even/odd:
  s_n = x_n + x_{7-n},  d_n = x_n - x_{7-n}    (4 each)
  Even outputs Y[0,2,4,6] from {s0..s3} (DCT-II of size 4).
  Odd outputs Y[1,3,5,7]  from {d0..d3} (DCT-IV of size 4).

Coefficient reductions (using cos(π-α) = -cos(α) and 2π periodicity):
  cv[k] = cos(πk/16)   for k=1..7
  cv[4] = √2/2         (== cos(π/4), special)

Output map (verified by hand and matches FFTW REDFT10 numerically):
  Y[0] = 2*(s0 + s1 + s2 + s3)
  Y[2] = 2*(cv[2]*(s0-s3) + cv[6]*(s1-s2))
  Y[4] = √2*(s0 - s1 - s2 + s3)             # absorbs the 2 * cv[4]
  Y[6] = 2*(cv[6]*(s0-s3) - cv[2]*(s1-s2))
  Y[1] = 2*(cv[1]*d0 + cv[3]*d1 + cv[5]*d2 + cv[7]*d3)
  Y[3] = 2*(cv[3]*d0 - cv[7]*d1 - cv[1]*d2 - cv[5]*d3)
  Y[5] = 2*(cv[5]*d0 - cv[1]*d1 + cv[7]*d2 + cv[3]*d3)
  Y[7] = 2*(cv[7]*d0 - cv[5]*d1 + cv[3]*d2 - cv[1]*d3)

Op count (per K-batch of 4 doubles, AVX2):
  8 pair add/sub + 4*(2 mul) for even-pair Y[2,6]
  + 4 ops for Y[0], 1 mul for Y[4]
  + 4*(4 mul) for odd Y[1,3,5,7]
  ≈ 28 muls + 22 adds + 8 final-by-2 muls = ~58 SIMD ops/4 K-batches.
  Per scalar transform: ~14-15 ops.
"""
import math
import sys

# Cosines: cv[k] = cos(πk/16) for k=1..7
CV = [math.cos(math.pi * k / 16.0) for k in range(8)]   # [cos(0)=1, cv1, cv2, ..., cv7]
SQRT2 = math.sqrt(2.0)


def emit_avx2():
    out = []
    out.append("/* Auto-generated by gen_dct8.py — DCT-II N=8 codelet (FFTW REDFT10).")
    out.append(" *   Y[k] = 2 * sum_{n=0..7} x[n] * cos(π k (2n+1) / 16)")
    out.append(" * Layout: in[n*K + k] for n=0..7, k=0..K-1; out same shape.")
    out.append(" * Processes K-batches; 4 doubles per AVX2 SIMD pass; scalar tail. */")
    out.append("")
    out.append("#ifndef DCT2_N8_AVX2_H")
    out.append("#define DCT2_N8_AVX2_H")
    out.append("#include <stddef.h>")
    out.append("#include <immintrin.h>")
    out.append("")
    out.append("__attribute__((target(\"avx2,fma\"))) static void")
    out.append("dct2_n8_avx2(const double * __restrict__ in,")
    out.append("             double * __restrict__ out,")
    out.append("             size_t K)")
    out.append("{")

    # Constants
    out.append(f"    const __m256d C1   = _mm256_set1_pd({CV[1]:.20f});")
    out.append(f"    const __m256d C2   = _mm256_set1_pd({CV[2]:.20f});")
    out.append(f"    const __m256d C3   = _mm256_set1_pd({CV[3]:.20f});")
    out.append(f"    const __m256d C5   = _mm256_set1_pd({CV[5]:.20f});")
    out.append(f"    const __m256d C6   = _mm256_set1_pd({CV[6]:.20f});")
    out.append(f"    const __m256d C7   = _mm256_set1_pd({CV[7]:.20f});")
    out.append(f"    const __m256d two  = _mm256_set1_pd(2.0);")
    out.append(f"    const __m256d sqrt2 = _mm256_set1_pd({SQRT2:.20f});")
    out.append("")
    out.append("    size_t k = 0;")
    out.append("    for (; k + 4 <= K; k += 4) {")
    # Loads
    for n in range(8):
        out.append(f"        __m256d x{n} = _mm256_loadu_pd(&in[{n}*K + k]);")
    out.append("")
    # Pair sums and diffs
    for n in range(4):
        out.append(f"        __m256d s{n} = _mm256_add_pd(x{n}, x{7-n});")
        out.append(f"        __m256d d{n} = _mm256_sub_pd(x{n}, x{7-n});")
    out.append("")
    # Even outputs
    out.append("        /* Y[0] = 2 * (s0+s1+s2+s3) */")
    out.append("        __m256d e_sum01 = _mm256_add_pd(s0, s1);")
    out.append("        __m256d e_sum23 = _mm256_add_pd(s2, s3);")
    out.append("        _mm256_storeu_pd(&out[0*K + k],")
    out.append("            _mm256_mul_pd(two, _mm256_add_pd(e_sum01, e_sum23)));")
    out.append("")
    out.append("        /* Y[4] = √2 * (s0 - s1 - s2 + s3)   (cos(π/4) absorbed; 2*cos(π/4) = √2) */")
    out.append("        __m256d s03 = _mm256_add_pd(s0, s3);")
    out.append("        __m256d s12 = _mm256_add_pd(s1, s2);")
    out.append("        _mm256_storeu_pd(&out[4*K + k],")
    out.append("            _mm256_mul_pd(sqrt2, _mm256_sub_pd(s03, s12)));")
    out.append("")
    out.append("        /* Y[2] = 2 * (C2*(s0-s3) + C6*(s1-s2)) */")
    out.append("        __m256d s0m3 = _mm256_sub_pd(s0, s3);")
    out.append("        __m256d s1m2 = _mm256_sub_pd(s1, s2);")
    out.append("        __m256d e2 = _mm256_fmadd_pd(C2, s0m3, _mm256_mul_pd(C6, s1m2));")
    out.append("        _mm256_storeu_pd(&out[2*K + k], _mm256_mul_pd(two, e2));")
    out.append("")
    out.append("        /* Y[6] = 2 * (C6*(s0-s3) - C2*(s1-s2)) */")
    out.append("        __m256d e6 = _mm256_fmsub_pd(C6, s0m3, _mm256_mul_pd(C2, s1m2));")
    out.append("        _mm256_storeu_pd(&out[6*K + k], _mm256_mul_pd(two, e6));")
    out.append("")
    # Odd outputs (FMA chains)
    out.append("        /* Y[1] = 2 * (C1*d0 + C3*d1 + C5*d2 + C7*d3) */")
    out.append("        __m256d o1 = _mm256_fmadd_pd(C1, d0, _mm256_mul_pd(C3, d1));")
    out.append("        o1 = _mm256_fmadd_pd(C5, d2, o1);")
    out.append("        o1 = _mm256_fmadd_pd(C7, d3, o1);")
    out.append("        _mm256_storeu_pd(&out[1*K + k], _mm256_mul_pd(two, o1));")
    out.append("")
    out.append("        /* Y[3] = 2 * (C3*d0 - C7*d1 - C1*d2 - C5*d3) */")
    out.append("        __m256d o3 = _mm256_fmsub_pd(C3, d0, _mm256_mul_pd(C7, d1));")
    out.append("        o3 = _mm256_fnmadd_pd(C1, d2, o3);")
    out.append("        o3 = _mm256_fnmadd_pd(C5, d3, o3);")
    out.append("        _mm256_storeu_pd(&out[3*K + k], _mm256_mul_pd(two, o3));")
    out.append("")
    out.append("        /* Y[5] = 2 * (C5*d0 - C1*d1 + C7*d2 + C3*d3) */")
    out.append("        __m256d o5 = _mm256_fmsub_pd(C5, d0, _mm256_mul_pd(C1, d1));")
    out.append("        o5 = _mm256_fmadd_pd(C7, d2, o5);")
    out.append("        o5 = _mm256_fmadd_pd(C3, d3, o5);")
    out.append("        _mm256_storeu_pd(&out[5*K + k], _mm256_mul_pd(two, o5));")
    out.append("")
    out.append("        /* Y[7] = 2 * (C7*d0 - C5*d1 + C3*d2 - C1*d3) */")
    out.append("        __m256d o7 = _mm256_fmsub_pd(C7, d0, _mm256_mul_pd(C5, d1));")
    out.append("        o7 = _mm256_fmadd_pd(C3, d2, o7);")
    out.append("        o7 = _mm256_fnmadd_pd(C1, d3, o7);")
    out.append("        _mm256_storeu_pd(&out[7*K + k], _mm256_mul_pd(two, o7));")
    out.append("    }")
    out.append("")
    # Scalar tail for K not a multiple of 4
    out.append("    /* Scalar tail */")
    out.append("    for (; k < K; k++) {")
    for n in range(8):
        out.append(f"        double x{n} = in[{n}*K + k];")
    for n in range(4):
        out.append(f"        double s{n} = x{n} + x{7-n};")
        out.append(f"        double d{n} = x{n} - x{7-n};")
    out.append(f"        const double c1={CV[1]:.20f}, c2={CV[2]:.20f}, c3={CV[3]:.20f};")
    out.append(f"        const double c5={CV[5]:.20f}, c6={CV[6]:.20f}, c7={CV[7]:.20f};")
    out.append(f"        const double sq2 = {SQRT2:.20f};")
    out.append("        out[0*K+k] = 2.0 * (s0 + s1 + s2 + s3);")
    out.append("        out[4*K+k] = sq2 * ((s0 + s3) - (s1 + s2));")
    out.append("        out[2*K+k] = 2.0 * (c2*(s0-s3) + c6*(s1-s2));")
    out.append("        out[6*K+k] = 2.0 * (c6*(s0-s3) - c2*(s1-s2));")
    out.append("        out[1*K+k] = 2.0 * (c1*d0 + c3*d1 + c5*d2 + c7*d3);")
    out.append("        out[3*K+k] = 2.0 * (c3*d0 - c7*d1 - c1*d2 - c5*d3);")
    out.append("        out[5*K+k] = 2.0 * (c5*d0 - c1*d1 + c7*d2 + c3*d3);")
    out.append("        out[7*K+k] = 2.0 * (c7*d0 - c5*d1 + c3*d2 - c1*d3);")
    out.append("    }")
    out.append("}")
    out.append("")
    out.append("#endif /* DCT2_N8_AVX2_H */")
    return "\n".join(out) + "\n"


if __name__ == "__main__":
    sys.stdout.write(emit_avx2())
