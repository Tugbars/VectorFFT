#!/usr/bin/env python3
"""
gen_dct3_n8.py — Specialized DCT-III N=8 codelet (FFTW REDFT01 convention).

Y[k] = X[0] + 2 * sum_{n=1..7} X[n] * cos(π n (2k+1) / 16)   for k=0..7

Algorithm — E/O decomposition (transpose of DCT-II's structure):
  E[k] = (Y[k] + Y[7-k]) / 2     uses only X[0], X[2], X[4], X[6]
  O[k] = (Y[k] - Y[7-k]) / 2     uses only X[1], X[3], X[5], X[7]
  Y[k]   = E[k] + O[k]
  Y[7-k] = E[k] - O[k]

Even part E[0..3]: factors as a 4-point DCT-III on {X[0], X[2], X[4], X[6]}.
  a = X[0] + 2*c4*X[4],  b = X[0] - 2*c4*X[4]
  p = c2*X[2] + c6*X[6],  q = c6*X[2] - c2*X[6]
  E[0] = a + 2p,  E[3] = a - 2p
  E[1] = b + 2q,  E[2] = b - 2q

Odd part O[0..3]: matrix-multiply on {X[1], X[3], X[5], X[7]} with
the SAME coefficient matrix as DCT-II's Y[1,3,5,7] (but applied to
direct inputs, not pair-diffs).
  O[0] = 2*(c1*X[1] + c3*X[3] + c5*X[5] + c7*X[7])
  O[1] = 2*(c3*X[1] - c7*X[3] - c1*X[5] - c5*X[7])
  O[2] = 2*(c5*X[1] - c1*X[3] + c7*X[5] + c3*X[7])
  O[3] = 2*(c7*X[1] - c5*X[3] + c3*X[5] - c1*X[7])

Op count (per scalar transform): ~21 muls + 29 add/sub ≈ 50 ops, matching
FFTW e01_8 ballpark.
"""
import math
import sys

CV = [math.cos(math.pi * k / 16.0) for k in range(8)]   # cv[0]=1, cv[k]=cos(πk/16)


def emit_avx2():
    out = []
    out.append("/* Auto-generated by gen_dct3_n8.py — DCT-III N=8 codelet (FFTW REDFT01).")
    out.append(" *   Y[k] = X[0] + 2 * sum_{n=1..7} X[n] * cos(π n (2k+1) / 16)")
    out.append(" * Layout: in[n*K + k] for n=0..7, k=0..K-1; out same shape. */")
    out.append("")
    out.append("#ifndef DCT3_N8_AVX2_H")
    out.append("#define DCT3_N8_AVX2_H")
    out.append("#include <stddef.h>")
    out.append("#include <immintrin.h>")
    out.append("")
    out.append("__attribute__((target(\"avx2,fma\"))) static void")
    out.append("dct3_n8_avx2(const double * __restrict__ in,")
    out.append("             double * __restrict__ out,")
    out.append("             size_t K)")
    out.append("{")
    # Coefficients: D_k = 2*cos(πk/16). The DCT-III formula already has the 2*
    # multiplier; bake it into the constant.
    out.append(f"    const __m256d D1 = _mm256_set1_pd({2.0*CV[1]:.20f});")
    out.append(f"    const __m256d D2 = _mm256_set1_pd({2.0*CV[2]:.20f});")
    out.append(f"    const __m256d D3 = _mm256_set1_pd({2.0*CV[3]:.20f});")
    out.append(f"    const __m256d D4 = _mm256_set1_pd({2.0*CV[4]:.20f});")
    out.append(f"    const __m256d D5 = _mm256_set1_pd({2.0*CV[5]:.20f});")
    out.append(f"    const __m256d D6 = _mm256_set1_pd({2.0*CV[6]:.20f});")
    out.append(f"    const __m256d D7 = _mm256_set1_pd({2.0*CV[7]:.20f});")
    out.append("")
    out.append("    size_t k = 0;")
    out.append("    for (; k + 4 <= K; k += 4) {")
    for n in range(8):
        out.append(f"        __m256d x{n} = _mm256_loadu_pd(&in[{n}*K + k]);")
    out.append("")
    # ──── Even part ────────────────────────────────────────────────
    out.append("        /* Even part E[0..3] (uses x0, x2, x4, x6) */")
    out.append("        __m256d t  = _mm256_mul_pd(D4, x4);     /* 2*c4 * X[4] */")
    out.append("        __m256d a  = _mm256_add_pd(x0, t);      /* X[0] + 2c4 X[4] */")
    out.append("        __m256d b  = _mm256_sub_pd(x0, t);      /* X[0] - 2c4 X[4] */")
    out.append("        __m256d p  = _mm256_fmadd_pd(D2, x2, _mm256_mul_pd(D6, x6));   /* 2c2 X[2] + 2c6 X[6] */")
    out.append("        __m256d q  = _mm256_fmsub_pd(D6, x2, _mm256_mul_pd(D2, x6));   /* 2c6 X[2] - 2c2 X[6] */")
    out.append("        __m256d E0 = _mm256_add_pd(a, p);")
    out.append("        __m256d E3 = _mm256_sub_pd(a, p);")
    out.append("        __m256d E1 = _mm256_add_pd(b, q);")
    out.append("        __m256d E2 = _mm256_sub_pd(b, q);")
    out.append("")
    # ──── Odd part ─────────────────────────────────────────────────
    out.append("        /* Odd part O[0..3] (uses x1, x3, x5, x7).")
    out.append("         * Same matrix shape as DCT-II's Y[1,3,5,7] applied to direct inputs. */")
    out.append("        __m256d O0 = _mm256_fmadd_pd(D1, x1, _mm256_mul_pd(D3, x3));")
    out.append("        O0 = _mm256_fmadd_pd(D5, x5, O0);")
    out.append("        O0 = _mm256_fmadd_pd(D7, x7, O0);")
    out.append("")
    out.append("        __m256d O1 = _mm256_fmsub_pd(D3, x1, _mm256_mul_pd(D7, x3));")
    out.append("        O1 = _mm256_fnmadd_pd(D1, x5, O1);")
    out.append("        O1 = _mm256_fnmadd_pd(D5, x7, O1);")
    out.append("")
    out.append("        __m256d O2 = _mm256_fmsub_pd(D5, x1, _mm256_mul_pd(D1, x3));")
    out.append("        O2 = _mm256_fmadd_pd(D7, x5, O2);")
    out.append("        O2 = _mm256_fmadd_pd(D3, x7, O2);")
    out.append("")
    out.append("        __m256d O3 = _mm256_fmsub_pd(D7, x1, _mm256_mul_pd(D5, x3));")
    out.append("        O3 = _mm256_fmadd_pd(D3, x5, O3);")
    out.append("        O3 = _mm256_fnmadd_pd(D1, x7, O3);")
    out.append("")
    # ──── Combine ──────────────────────────────────────────────────
    out.append("        /* Y[k] = E[k] + O[k], Y[7-k] = E[k] - O[k] for k=0..3 */")
    out.append("        _mm256_storeu_pd(&out[0*K + k], _mm256_add_pd(E0, O0));")
    out.append("        _mm256_storeu_pd(&out[7*K + k], _mm256_sub_pd(E0, O0));")
    out.append("        _mm256_storeu_pd(&out[1*K + k], _mm256_add_pd(E1, O1));")
    out.append("        _mm256_storeu_pd(&out[6*K + k], _mm256_sub_pd(E1, O1));")
    out.append("        _mm256_storeu_pd(&out[2*K + k], _mm256_add_pd(E2, O2));")
    out.append("        _mm256_storeu_pd(&out[5*K + k], _mm256_sub_pd(E2, O2));")
    out.append("        _mm256_storeu_pd(&out[3*K + k], _mm256_add_pd(E3, O3));")
    out.append("        _mm256_storeu_pd(&out[4*K + k], _mm256_sub_pd(E3, O3));")
    out.append("    }")
    out.append("")
    # Scalar tail
    out.append("    /* Scalar tail */")
    out.append("    for (; k < K; k++) {")
    for n in range(8):
        out.append(f"        double x{n} = in[{n}*K + k];")
    out.append(f"        const double d1={2.0*CV[1]:.20f}, d2={2.0*CV[2]:.20f}, d3={2.0*CV[3]:.20f};")
    out.append(f"        const double d4={2.0*CV[4]:.20f}, d5={2.0*CV[5]:.20f}, d6={2.0*CV[6]:.20f}, d7={2.0*CV[7]:.20f};")
    out.append("        double t = d4*x4;")
    out.append("        double a = x0 + t, b = x0 - t;")
    out.append("        double p = d2*x2 + d6*x6;")
    out.append("        double q = d6*x2 - d2*x6;")
    out.append("        double E0=a+p, E3=a-p, E1=b+q, E2=b-q;")
    out.append("        double O0 = d1*x1 + d3*x3 + d5*x5 + d7*x7;")
    out.append("        double O1 = d3*x1 - d7*x3 - d1*x5 - d5*x7;")
    out.append("        double O2 = d5*x1 - d1*x3 + d7*x5 + d3*x7;")
    out.append("        double O3 = d7*x1 - d5*x3 + d3*x5 - d1*x7;")
    out.append("        out[0*K+k]=E0+O0;  out[7*K+k]=E0-O0;")
    out.append("        out[1*K+k]=E1+O1;  out[6*K+k]=E1-O1;")
    out.append("        out[2*K+k]=E2+O2;  out[5*K+k]=E2-O2;")
    out.append("        out[3*K+k]=E3+O3;  out[4*K+k]=E3-O3;")
    out.append("    }")
    out.append("}")
    out.append("")
    out.append("#endif /* DCT3_N8_AVX2_H */")
    return "\n".join(out) + "\n"


if __name__ == "__main__":
    sys.stdout.write(emit_avx2())
