#!/usr/bin/env python3
"""
translate_genfft_tw.py — Translate FFTW genfft scalar twiddle codelet to AVX-512 split format.
"""
import re, sys, os

def extract_fma_body(fftw_src, radix):
    path = os.path.join(fftw_src, 'dft', 'scalar', 'codelets', f't1_{radix}.c')
    if not os.path.exists(path):
        print(f"Error: {path} not found", file=sys.stderr); sys.exit(1)
    with open(path) as f: code = f.read()
    m = re.search(r'#if defined\(ARCH_PREFERS_FMA\).*?\n(.*?)#else', code, re.DOTALL)
    if not m:
        print(f"Error: FMA variant not found in {path}", file=sys.stderr); sys.exit(1)
    return m.group(1)

def parse_body(body, radix):
    lines = body.split('\n')
    ops = []; constants = {}; var_decls = []
    load_ri = {}; load_ii = {}; load_w = {}
    store_ri = {}; store_ii = {}
    for line in lines:
        line = line.strip().rstrip(';').strip()
        if not line or line.startswith('//') or line.startswith('/*') or line.startswith('*'): continue
        if line.startswith('{') or line.startswith('}') or 'MAKE_VOLATILE' in line: continue
        if 'INT m' in line or 'for (m' in line or 'E ' == line[:2]:
            if line.startswith('E '):
                names = [n.strip() for n in line[2:].split(',')]
                var_decls.extend(names)
            continue
        if line.startswith('DK('):
            m = re.match(r'DK\((\w+),\s*([^)]+)\)', line)
            if m: constants[m.group(1)] = m.group(2).strip()
            continue
        m = re.match(r'(\w+)\s*=\s*(.+)', line)
        if not m:
            m = re.match(r'(ri|ii)\[(?:WS\(rs,\s*(\d+)\)|(\d+))\]\s*=\s*(.+)', line)
            if m:
                arr = m.group(1); idx = int(m.group(2)) if m.group(2) else int(m.group(3))
                expr = m.group(4)
                if arr == 'ri': store_ri[idx] = expr
                else: store_ii[idx] = expr
                ops.append(('STORE', arr, idx, expr))
            continue
        target = m.group(1); expr = m.group(2)
        lm = re.match(r'(ri|ii)\[(?:WS\(rs,\s*(\d+)\)|(\d+))\]', expr)
        if lm:
            arr = lm.group(1); idx = int(lm.group(2)) if lm.group(2) else int(lm.group(3))
            if arr == 'ri': load_ri[target] = idx
            else: load_ii[target] = idx
            ops.append(('LOAD', target, arr, idx)); continue
        wm = re.match(r'W\[(\d+)\]', expr)
        if wm: load_w[target] = int(wm.group(1)); ops.append(('LOAD_W', target, int(wm.group(1)))); continue
        fm = re.match(r'FMA\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
        if fm: ops.append(('FMA', target, fm.group(1).strip(), fm.group(2).strip(), fm.group(3).strip())); continue
        fm = re.match(r'FNMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
        if fm: ops.append(('FNMS', target, fm.group(1).strip(), fm.group(2).strip(), fm.group(3).strip())); continue
        fm = re.match(r'FMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
        if fm: ops.append(('FMS', target, fm.group(1).strip(), fm.group(2).strip(), fm.group(3).strip())); continue
        fm = re.match(r'FNMA\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
        if fm: ops.append(('FNMA', target, fm.group(1).strip(), fm.group(2).strip(), fm.group(3).strip())); continue
        bm = re.match(r'(.+?)\s*([+\-*])\s*(.+)', expr)
        if bm:
            lhs = bm.group(1).strip(); op = bm.group(2); rhs = bm.group(3).strip()
            ops.append(('BINOP', target, op, lhs, rhs)); continue
        nm = re.match(r'-\s*(\w+)', expr)
        if nm: ops.append(('NEG', target, nm.group(1).strip())); continue
        ops.append(('UNKNOWN', target, expr))
    return ops, constants, var_decls

def emit_codelet(ops, constants, var_decls, radix, isa='avx512'):
    if isa == 'avx512': T, C, P = '__m512d', 8, '_mm512'; attr = '__attribute__((target("avx512f,fma")))'; use_intrin = True
    elif isa == 'avx2': T, C, P = '__m256d', 4, '_mm256'; attr = '__attribute__((target("avx2,fma")))'; use_intrin = True
    else: T, C, P = 'double', 1, None; attr = ''; use_intrin = False
    R = radix; ISA_U = isa.upper()
    cpfx = f'R{R}D_'
    prefixed_constants = {cpfx + name: val for name, val in constants.items()}
    const_names = set(prefixed_constants.keys())
    const_rename = {name: cpfx + name for name in constants}
    def bcast(arg):
        arg = arg.strip(); arg = const_rename.get(arg, arg)
        if not use_intrin: return arg
        if arg in const_names: return f'{P}_set1_pd({arg})'
        return arg
    guard = f'FFT_RADIX{R}_{ISA_U}_TW_DAG_H'
    L = []
    L.append(f'/**'); L.append(f' * @file fft_radix{R}_{isa}_tw_dag.h')
    L.append(f' * @brief DFT-{R} {ISA_U} split twiddle codelet — genfft DAG + FMA')
    L.append(f' *')
    L.append(f' * Mathematical DAG from FFTW genfft (Frigo & Johnson, 1999/2005).')
    L.append(f' * ISA post-processing: {ISA_U} vectorization across K (k-step={C}),')
    L.append(f' * split re/im layout, native FMA intrinsics.')
    L.append(f' *')
    L.append(f' * Generated by translate_genfft_tw.py from FFTW t1_{R} FMA codelet.')
    L.append(f' */'); L.append(f'')
    L.append(f'#ifndef {guard}'); L.append(f'#define {guard}')
    L.append(f'#include <stddef.h>')
    if use_intrin: L.append(f'#include <immintrin.h>')
    L.append(f'')
    L.append(f'#ifndef FFT_RADIX{R}_DAG_CONSTANTS'); L.append(f'#define FFT_RADIX{R}_DAG_CONSTANTS')
    L.append(f'/* genfft constants — prefixed {cpfx} to avoid shadowing mono IL */')
    for name, val in sorted(prefixed_constants.items()):
        L.append(f'static const double {name} = {val};')
    L.append(f'#endif'); L.append(f'')
    if attr: L.append(attr)
    L.append(f'static void'); L.append(f'radix{R}_tw_dag_dit_fwd_{isa}(')
    L.append(f'    const double * __restrict__ in_re, const double * __restrict__ in_im,')
    L.append(f'    double * __restrict__ out_re, double * __restrict__ out_im,')
    L.append(f'    const double * __restrict__ tw_re, const double * __restrict__ tw_im,')
    L.append(f'    size_t K)')
    L.append(f'{{')
    L.append(f'    for (size_t k = 0; k < K; k += {C}) {{')
    indent = '        '; declared = set()
    def decl(var):
        if var not in declared: declared.add(var); return f'{T} {var}'
        return var
    for op in ops:
        if op[0] == 'LOAD':
            _, target, arr, idx = op
            if use_intrin:
                src = f'&in_{"re" if arr=="ri" else "im"}[{idx}*K+k]'
                L.append(f'{indent}{decl(target)} = {P}_load_pd({src});')
            else:
                src = f'in_{"re" if arr=="ri" else "im"}[{idx}*K+k]'
                L.append(f'{indent}{decl(target)} = {src};')
        elif op[0] == 'LOAD_W':
            _, target, widx = op; tw_elem = widx // 2; is_im = widx % 2
            if use_intrin:
                if is_im:
                    L.append(f'{indent}{decl(target)} = {P}_xor_pd({P}_load_pd(&tw_im[{tw_elem}*K+k]), {P}_set1_pd(-0.0));')
                else:
                    L.append(f'{indent}{decl(target)} = {P}_load_pd(&tw_re[{tw_elem}*K+k]);')
            else:
                if is_im:
                    L.append(f'{indent}{decl(target)} = -tw_im[{tw_elem}*K+k];')
                else:
                    L.append(f'{indent}{decl(target)} = tw_re[{tw_elem}*K+k];')
        elif op[0] == 'STORE':
            _, arr, idx, expr = op
            renamed_expr = expr
            for orig, pref in const_rename.items(): renamed_expr = renamed_expr.replace(orig, pref)
            expr_t = translate_expr(renamed_expr, const_names, P) if use_intrin else translate_expr_scalar(renamed_expr)
            dst = f'out_{"re" if arr=="ri" else "im"}[{idx}*K+k]'
            if use_intrin: L.append(f'{indent}{P}_store_pd(&{dst}, {expr_t});')
            else: L.append(f'{indent}{dst} = {expr_t};')
        elif op[0] == 'FMA':
            _, target, a, b, c = op
            if use_intrin: L.append(f'{indent}{decl(target)} = {P}_fmadd_pd({bcast(a)}, {bcast(b)}, {bcast(c)});')
            else: L.append(f'{indent}{decl(target)} = {bcast(a)} * {bcast(b)} + {bcast(c)};')
        elif op[0] == 'FNMS':
            _, target, a, b, c = op
            if use_intrin: L.append(f'{indent}{decl(target)} = {P}_fnmadd_pd({bcast(a)}, {bcast(b)}, {bcast(c)});')
            else: L.append(f'{indent}{decl(target)} = {bcast(c)} - {bcast(a)} * {bcast(b)};')
        elif op[0] == 'FMS':
            _, target, a, b, c = op
            if use_intrin: L.append(f'{indent}{decl(target)} = {P}_fmsub_pd({bcast(a)}, {bcast(b)}, {bcast(c)});')
            else: L.append(f'{indent}{decl(target)} = {bcast(a)} * {bcast(b)} - {bcast(c)};')
        elif op[0] == 'FNMA':
            _, target, a, b, c = op
            if use_intrin: L.append(f'{indent}{decl(target)} = {P}_fnmsub_pd({bcast(a)}, {bcast(b)}, {bcast(c)});')
            else: L.append(f'{indent}{decl(target)} = -({bcast(a)} * {bcast(b)}) - {bcast(c)};')
        elif op[0] == 'BINOP':
            _, target, op_sym, lhs, rhs = op
            if use_intrin:
                fn = {'+':"add",'-':"sub",'*':"mul"}[op_sym]
                L.append(f'{indent}{decl(target)} = {P}_{fn}_pd({bcast(lhs)}, {bcast(rhs)});')
            else: L.append(f'{indent}{decl(target)} = {bcast(lhs)} {op_sym} {bcast(rhs)};')
        elif op[0] == 'NEG':
            _, target, src = op
            if use_intrin: L.append(f'{indent}{decl(target)} = {P}_xor_pd({src}, {P}_set1_pd(-0.0));')
            else: L.append(f'{indent}{decl(target)} = -{src};')
        elif op[0] == 'UNKNOWN': L.append(f'{indent}/* UNKNOWN: {op[1]} = {op[2]} */')
    L.append(f'    }}'); L.append(f'}}'); L.append(f'')
    L.append(f'/* DIT backward: pointer-swap trick (Frigo & Johnson).')
    L.append(f' * Swapping re↔im on input+output converts forward DFT to backward. */')
    if attr: L.append(attr)
    L.append(f'static inline void'); L.append(f'radix{R}_tw_dag_dit_bwd_{isa}(')
    L.append(f'    const double * __restrict__ in_re, const double * __restrict__ in_im,')
    L.append(f'    double * __restrict__ out_re, double * __restrict__ out_im,')
    L.append(f'    const double * __restrict__ tw_re, const double * __restrict__ tw_im,')
    L.append(f'    size_t K)')
    L.append(f'{{')
    L.append(f'    radix{R}_tw_dag_dit_fwd_{isa}(in_im, in_re, out_im, out_re, tw_re, tw_im, K);')
    L.append(f'}}'); L.append(f'')
    L.append(f'#endif /* {guard} */')
    return L

def translate_expr(expr, const_names=set(), P='_mm512'):
    """Recursively translate store expressions to AVX intrinsics."""
    expr = expr.strip()
    def bc(arg):
        arg = arg.strip()
        if arg in const_names: return f'{P}_set1_pd({arg})'
        return arg
    # FMA/FNMS/FMS
    m = re.match(r'FMA\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{P}_fmadd_pd({bc(m.group(1).strip())}, {bc(m.group(2).strip())}, {bc(m.group(3).strip())})'
    m = re.match(r'FNMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{P}_fnmadd_pd({bc(m.group(1).strip())}, {bc(m.group(2).strip())}, {bc(m.group(3).strip())})'
    m = re.match(r'FMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{P}_fmsub_pd({bc(m.group(1).strip())}, {bc(m.group(2).strip())}, {bc(m.group(3).strip())})'
    # Single variable
    if re.match(r'^\w+$', expr): return bc(expr)
    # Multi-term: split at last + or - (right-to-left) to handle A + B + C + D
    for i in range(len(expr)-1, -1, -1):
        if expr[i] in '+-':
            lhs = expr[:i].strip()
            rhs = expr[i+1:].strip()
            op = expr[i]
            if lhs and rhs:
                fn = 'add' if op == '+' else 'sub'
                return f'{P}_{fn}_pd({translate_expr(lhs, const_names, P)}, {translate_expr(rhs, const_names, P)})'
    # A * B
    m = re.match(r'(\w+)\s*\*\s*(\w+)', expr)
    if m: return f'{P}_mul_pd({bc(m.group(1))}, {bc(m.group(2))})'
    return f'/* UNTRANSLATED: {expr} */'

def translate_expr_scalar(expr):
    """Recursively translate store expressions to scalar C."""
    expr = expr.strip()
    m = re.match(r'FMA\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{m.group(1).strip()} * {m.group(2).strip()} + {m.group(3).strip()}'
    m = re.match(r'FNMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{m.group(3).strip()} - {m.group(1).strip()} * {m.group(2).strip()}'
    m = re.match(r'FMS\(([^,]+),\s*([^,]+),\s*([^)]+)\)', expr)
    if m: return f'{m.group(1).strip()} * {m.group(2).strip()} - {m.group(3).strip()}'
    if re.match(r'^\w+$', expr): return expr
    # Multi-term: split at last + or -
    for i in range(len(expr)-1, -1, -1):
        if expr[i] in '+-':
            lhs = expr[:i].strip()
            rhs = expr[i+1:].strip()
            if lhs and rhs:
                return f'{translate_expr_scalar(lhs)} {expr[i]} {translate_expr_scalar(rhs)}'
    m = re.match(r'(\w+)\s*\*\s*(\w+)', expr)
    if m: return f'{m.group(1)} * {m.group(2)}'
    return f'/* UNTRANSLATED: {expr} */'

def main():
    if len(sys.argv) < 3:
        print(f"Usage: {sys.argv[0]} <fftw_src_dir> <radix> [scalar|avx2|avx512]", file=sys.stderr); sys.exit(1)
    fftw_src = sys.argv[1]; radix = int(sys.argv[2])
    isa = sys.argv[3] if len(sys.argv) > 3 else 'avx512'
    if isa not in ('scalar', 'avx2', 'avx512'):
        print(f"Unknown ISA: {isa}. Use scalar, avx2 or avx512.", file=sys.stderr); sys.exit(1)
    body = extract_fma_body(fftw_src, radix)
    ops, constants, var_decls = parse_body(body, radix)
    n_fma = sum(1 for o in ops if o[0] in ('FMA', 'FNMS', 'FMS', 'FNMA'))
    n_binop = sum(1 for o in ops if o[0] == 'BINOP')
    n_load = sum(1 for o in ops if o[0] == 'LOAD')
    n_tw = sum(1 for o in ops if o[0] == 'LOAD_W')
    n_store = sum(1 for o in ops if o[0] == 'STORE')
    n_unk = sum(1 for o in ops if o[0] == 'UNKNOWN')
    print(f"/* DAG stats: {n_fma} FMA, {n_binop} ADD/SUB/MUL, {n_load} data loads, {n_tw} twiddle loads, {n_store} stores, {n_unk} unknown */", file=sys.stderr)
    print(f"/* Variables: {len(var_decls)}, Constants: {len(constants)}, ISA: {isa} */", file=sys.stderr)
    lines = emit_codelet(ops, constants, var_decls, radix, isa)
    print('\n'.join(lines))

if __name__ == '__main__':
    main()
