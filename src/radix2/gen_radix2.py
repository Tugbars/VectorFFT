#!/usr/bin/env python3
"""
gen_radix2.py — DFT-2 standalone codelets for VectorFFT

The simplest butterfly: out[0] = in[0] ± in[1]
Zero constants. Forward notw = backward notw (DFT-2 is self-inverse).
Twiddle table: 1*K doubles per component (only W^1).

Usage: python3 gen_radix2.py avx512|avx2|scalar
"""
import sys

ISA = {
    'avx512': {'VL':8,'V':'__m512d','p':'_mm512',
        'target':'__attribute__((target("avx512f,fma")))',
        'guard_begin':'#if defined(__AVX512F__) || defined(__AVX512F)',
        'guard_end':'#endif /* __AVX512F__ */',
        'ld_macro':'#define R2_LD(p) _mm512_loadu_pd(p)\n#define R2_ST(p,v) _mm512_storeu_pd((p),(v))'},
    'avx2': {'VL':4,'V':'__m256d','p':'_mm256',
        'target':'__attribute__((target("avx2,fma")))',
        'guard_begin':'#ifdef __AVX2__',
        'guard_end':'#endif /* __AVX2__ */',
        'ld_macro':'#define R2_LD(p) _mm256_loadu_pd(p)\n#define R2_ST(p,v) _mm256_storeu_pd((p),(v))'},
    'scalar': {'VL':1,'V':'double','p':'',
        'target':'',
        'guard_begin':'',
        'guard_end':'',
        'ld_macro':''},
}

def gen_scalar_notw():
    return """    for (size_t k = 0; k < K; k++) {
        const double x0r = in_re[k], x0i = in_im[k];
        const double x1r = in_re[K+k], x1i = in_im[K+k];
        out_re[k]   = x0r + x1r;
        out_im[k]   = x0i + x1i;
        out_re[K+k] = x0r - x1r;
        out_im[K+k] = x0i - x1i;
    }"""

def gen_scalar_tw(direction):
    fwd = direction == 'fwd'
    return f"""    for (size_t k = 0; k < K; k++) {{
        const double x0r = in_re[k], x0i = in_im[k];
        const double r1r = in_re[K+k], r1i = in_im[K+k];
        const double wr = tw_re[k], wi = tw_im[k];
        const double x1r = {'r1r*wr - r1i*wi' if fwd else 'r1r*wr + r1i*wi'};
        const double x1i = {'r1r*wi + r1i*wr' if fwd else '-r1r*wi + r1i*wr'};
        out_re[k]   = x0r + x1r;
        out_im[k]   = x0i + x1i;
        out_re[K+k] = x0r - x1r;
        out_im[K+k] = x0i - x1i;
    }}"""

def gen_simd_notw(isa_name):
    I = ISA[isa_name]
    V, p = I['V'], I['p']
    return f"""    for (size_t k = 0; k < K; k += {I['VL']}) {{
        const {V} x0r = R2_LD(&in_re[k]),   x0i = R2_LD(&in_im[k]);
        const {V} x1r = R2_LD(&in_re[K+k]), x1i = R2_LD(&in_im[K+k]);
        R2_ST(&out_re[k],   {p}_add_pd(x0r, x1r));
        R2_ST(&out_im[k],   {p}_add_pd(x0i, x1i));
        R2_ST(&out_re[K+k], {p}_sub_pd(x0r, x1r));
        R2_ST(&out_im[K+k], {p}_sub_pd(x0i, x1i));
    }}"""

def gen_simd_tw(isa_name, direction):
    I = ISA[isa_name]
    V, p = I['V'], I['p']
    fwd = direction == 'fwd'
    mul = f'{p}_mul_pd'
    # fwd: x1r = r1r*wr - r1i*wi = fnmadd(r1i, wi, mul(r1r, wr))
    #       x1i = r1r*wi + r1i*wr = fmadd(r1r, wi, mul(r1i, wr))
    # bwd: x1r = r1r*wr + r1i*wi = fmadd(r1i, wi, mul(r1r, wr))
    #       x1i = -r1r*wi + r1i*wr = fnmadd(r1r, wi, mul(r1i, wr))
    if fwd:
        cmul_re = f'{p}_fnmadd_pd(r1i, wi, {mul}(r1r, wr))'
        cmul_im = f'{p}_fmadd_pd(r1r, wi, {mul}(r1i, wr))'
    else:
        cmul_re = f'{p}_fmadd_pd(r1i, wi, {mul}(r1r, wr))'
        cmul_im = f'{p}_fnmadd_pd(r1r, wi, {mul}(r1i, wr))'

    return f"""    for (size_t k = 0; k < K; k += {I['VL']}) {{
        const {V} x0r = R2_LD(&in_re[k]),   x0i = R2_LD(&in_im[k]);
        const {V} r1r = R2_LD(&in_re[K+k]), r1i = R2_LD(&in_im[K+k]);
        const {V} wr  = R2_LD(&tw_re[k]),   wi  = R2_LD(&tw_im[k]);
        const {V} x1r = {cmul_re};
        const {V} x1i = {cmul_im};
        R2_ST(&out_re[k],   {p}_add_pd(x0r, x1r));
        R2_ST(&out_im[k],   {p}_add_pd(x0i, x1i));
        R2_ST(&out_re[K+k], {p}_sub_pd(x0r, x1r));
        R2_ST(&out_im[K+k], {p}_sub_pd(x0i, x1i));
    }}"""

def gen_file(isa_name):
    I = ISA[isa_name]
    guard = f'FFT_RADIX2_{isa_name.upper()}_H'
    is_scalar = isa_name == 'scalar'

    parts = []
    parts.append(f'''/**
 * @file fft_radix2_{isa_name}.h
 * @brief DFT-2 {isa_name.upper()} codelets (notw + twiddled)
 *
 * The simplest butterfly: out[0] = x0 + x1, out[1] = x0 - x1.
 * Zero constants. Forward notw = backward notw (self-inverse).
 * Twiddle table: tw_re[k] = Re(W_{{2K}}^k), 1*K doubles per component.
 *
 * Generated by gen_radix2.py — do not edit.
 */

#ifndef {guard}
#define {guard}

{I['guard_begin']}

#include <stddef.h>''')

    if not is_scalar:
        parts.append('#include <immintrin.h>')
        parts.append('')
        parts.append(I['ld_macro'])

    # notw — same for fwd and bwd
    tw_params = ''
    for direction in ['fwd', 'bwd']:
        parts.append(f'''
{I['target']}
static inline void
radix2_notw_dit_kernel_{direction}_{isa_name}(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    size_t K)
{{''')
        if is_scalar:
            parts.append(gen_scalar_notw())
        else:
            parts.append(gen_simd_notw(isa_name))
        parts.append('}')

    # tw
    for direction in ['fwd', 'bwd']:
        parts.append(f'''
{I['target']}
static inline void
radix2_tw_dit_kernel_{direction}_{isa_name}(
    const double * __restrict__ in_re, const double * __restrict__ in_im,
    double * __restrict__ out_re, double * __restrict__ out_im,
    const double * __restrict__ tw_re, const double * __restrict__ tw_im,
    size_t K)
{{''')
        if is_scalar:
            parts.append(gen_scalar_tw(direction))
        else:
            parts.append(gen_simd_tw(isa_name, direction))
        parts.append('}')

    if not is_scalar:
        parts.append('\n#undef R2_LD\n#undef R2_ST')

    parts.append(f'\n{I["guard_end"]}')
    parts.append(f'#endif /* {guard} */')
    return '\n'.join(parts)

if __name__ == '__main__':
    if len(sys.argv) != 2 or sys.argv[1] not in ISA:
        print(f'Usage: {sys.argv[0]} avx512|avx2|scalar', file=sys.stderr)
        sys.exit(1)
    print(gen_file(sys.argv[1]))
