(* emit_profile_h.ml — emit a production-shaped radix_profile.h from the DAG.
 *
 * Usage:
 *   dune exec bin/emit_profile_h.exe -- --isa avx2   > cost_model/generated/radix_profile.h
 *   dune exec bin/emit_profile_h.exe -- --isa avx512 >> cost_model/generated/radix_profile.h
 *   dune exec bin/emit_profile_h.exe -- --isa both   > cost_model/generated/radix_profile.h
 *
 * Output is byte-compatible with the layout of src/core/generated/radix_profile.h
 * so a `diff` against production tells you exactly how the OCaml DAG's op
 * counts compare against the regex-derived Python counts. *)

(* Production radix sweep (from tools/radix_profile/extract.py and the
 * generate_codelets.sh family lists). t1s is intentionally absent —
 * see counts_for below. *)
(* Every radix with a codelet under src/prototype/codelets/. Includes the
 * large/xl pow2 set (128..1024) which production's radix_profile only
 * went up to 64 — the prototype emitter has them via Cooley_Tukey in
 * Dft.ml (128=8x16, 256=16x16, 512=16x32, 1024=32x32). t1s is absent
 * because the math layer t1=t1s; see counts_for. *)
let radixes_n1  = [2; 3; 4; 5; 6; 7; 8; 10; 11; 12; 13; 16; 17; 19; 20; 25; 32; 64; 128; 256; 512; 1024]
let radixes_t1  = [2; 4; 5; 6; 7; 8; 10; 11; 12; 13; 16; 17; 19; 20; 25; 32; 64; 128; 256; 512; 1024]

(* Trig codelet inventory under src/prototype/codelets/{isa}/trig/.
 * Identical across both ISAs; populated by scripts/generate_codelets.sh
 * via gen_radix.ml --dct2/--dct3/--dct4/--dst2/--dst3/--dht. *)
let trig_sizes = [8; 16; 32; 64]
let trig_variants = ["dct2"; "dct3"; "dct4"; "dst2"; "dst3"; "dht"]

(* Pretty-print a single radix entry in the C-aggregate-initializer
 * format production uses ([ N] = { .n_add = X, ... }). *)
let print_entry (r : int) (c : Vfft_v2.Profile.op_counts) =
  let open Vfft_v2.Profile in
  Printf.printf
    "    [%4d] = { .n_add = %5d, .n_mul = %5d, .n_fma = %5d, .n_load = %5d, .n_store = %5d, .n_blend = %3d, .n_set1 = %3d, .n_xor = %3d, .n_decls = %6d },\n"
    r c.n_add c.n_mul c.n_fma c.n_load c.n_store c.n_blend c.n_set1 c.n_xor c.n_decls

(* Generate the assignment list for one (R, variant), returning op_counts.
 * Returns None when the variant doesn't apply to that radix (e.g. t1 for
 * R=3 which is a Direct prime).
 *
 * t1s is intentionally absent: at the math layer t1 and t1s produce
 * IDENTICAL assignment lists. The t1s variant is purely an emit-time
 * choice — render twiddle loads as `_mm_set1_pd(scalar)` instead of
 * `_mm_loadu_pd(&vec)`. Same DAG, different rendering. The static op
 * counts as derived from the DAG would therefore be identical for t1
 * and t1s (n_load shifts to n_set1 in the rendered C, but not in the
 * DAG node kinds). If you need the renamed counts as production
 * regex sees them, parse the emitted C. *)
let counts_for (r : int) (variant : string) : Vfft_v2.Profile.op_counts option =
  let assigns =
    match variant with
    | "n1" -> (try Some (Vfft_v2.Dft.dft_expand r) with _ -> None)
    | "t1" -> (try Some (Vfft_v2.Dft.dft_expand_twiddled r) with _ -> None)
    (* Trig transforms — same hash-cons + dedup pipeline as n1/t1.
     * Matches the v1 pipeline used by gen_radix.ml before the optional
     * factor/share passes; gives a stable baseline that compares apples-
     * to-apples with the C2C profile rows. *)
    | "dct2" -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dct2 r) with _ -> None)
    | "dct3" -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dct3 r) with _ -> None)
    | "dct4" -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dct4 r) with _ -> None)
    | "dst2" -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dst2 r) with _ -> None)
    | "dst3" -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dst3 r) with _ -> None)
    | "dht"  -> (try Some (Vfft_v2.Dft_r2c.dft_expand_dht r)  with _ -> None)
    | other -> failwith ("emit_profile_h: unknown variant " ^ other)
  in
  match assigns with
  | None -> None
  | Some assigns ->
    Vfft_v2.Algsimp.reset ();
    (* needs_reassoc only knows about the C2C radix sweep — for trig
     * transforms it returns false (the default match arm), which is
     * what we want. Same pipeline path as n1. *)
    let reassoc = Vfft_v2.Dft.needs_reassoc r in
    let simplified = Vfft_v2.Algsimp.of_assignments ~reassoc assigns in
    let deduped = Vfft_v2.Algsimp.dedup_sub_pairs simplified in
    Some (Vfft_v2.Profile.count_ops deduped)

let emit_table ~(isa : string) ~(variant : string) (radixes : int list) =
  Printf.printf
    "static const stride_radix_profile_t stride_radix_profile_%s_%s[STRIDE_RADIX_PROFILE_MAX_R] = {\n"
    variant isa;
  List.iter (fun r ->
    match counts_for r variant with
    | Some c -> print_entry r c
    | None   -> ()       (* missing slot — leave zero-initialized *)
  ) radixes;
  Printf.printf "};\n\n"

let emit_header ~isas =
  print_endline "/* radix_profile.h — auto-generated by bin/emit_profile_h.ml.";
  print_endline " * DO NOT EDIT BY HAND. Regenerate via:";
  print_endline " *   dune exec bin/emit_profile_h.exe -- --isa both \\";
  print_endline " *     > cost_model/generated/radix_profile.h";
  print_endline " *";
  print_endline " * Per-radix arithmetic count + register-pressure proxy for codelet";
  print_endline " * variants emitted by the OCaml DAG compiler (Vfft_v2). Counts are";
  print_endline " * derived directly from the hash-consed Algsimp.t DAG via Profile.";
  print_endline " * count_ops — no regex over emitted C source.";
  print_endline " *";
  print_endline " * The shape mirrors src/core/generated/radix_profile.h so the two";
  print_endline " * headers can be diff-compared. Counts WILL differ from production's";
  print_endline " * (different generator → different code), and that divergence is the";
  print_endline " * useful signal.";
  print_endline " *";
  print_endline " * KNOWN LIMITATION — FMA undercount: the n_fma column reflects only";
  print_endline " * explicit NK_Fma nodes produced by the OCaml fma_lift pass. The C";
  print_endline " * compiler fuses additional mul+add pairs at codegen, so the assembled";
  print_endline " * FMA count exceeds this column by a size-dependent factor (~1.0× at";
  print_endline " * R=4, ~1.5× at R=64, ~2× at R=1024 under gcc-15 -O3 -mavx2 -mfma).";
  print_endline " * Total op count is roughly preserved; the per-column split is not";
  print_endline " * faithful for big radixes. See cost_model/README.md §\"Known limitations\". */";
  print_endline "#ifndef STRIDE_RADIX_PROFILE_H";
  print_endline "#define STRIDE_RADIX_PROFILE_H";
  print_endline "";
  print_endline "#define STRIDE_RADIX_PROFILE_MAX_R 1025";
  print_endline "";
  print_endline "typedef struct {";
  print_endline "    int n_add;";
  print_endline "    int n_mul;";
  print_endline "    int n_fma;";
  print_endline "    int n_load;";
  print_endline "    int n_store;";
  print_endline "    int n_blend;";
  print_endline "    int n_set1;";
  print_endline "    int n_xor;";
  print_endline "    int n_decls;";
  print_endline "} stride_radix_profile_t;";
  print_endline "";
  List.iter (fun isa ->
    emit_table ~isa ~variant:"n1" radixes_n1;
    emit_table ~isa ~variant:"t1" radixes_t1;
    (* Trig profile tables — one per (transform, isa). Indexed by N
     * (the transform size), same array-of-struct layout as the C2C
     * tables so the same _stride_total_ops helper applies. *)
    List.iter (fun t -> emit_table ~isa ~variant:t trig_sizes) trig_variants
  ) isas;
  (* Helper used by factorizer.h to compute fallback cost when the CPE
   * slot is empty. Sums all op counters into a single "work" estimate
   * (ops/SIMD-width gives a per-butterfly cycle approximation). *)
  print_endline "static inline int _stride_total_ops(const stride_radix_profile_t *p) {";
  print_endline "    return p->n_add + p->n_mul + p->n_fma + p->n_load + p->n_store";
  print_endline "         + p->n_blend + p->n_set1 + p->n_xor;";
  print_endline "}";
  print_endline "";
  print_endline "#endif /* STRIDE_RADIX_PROFILE_H */"

let () =
  let isa_arg = ref "avx2" in
  let i = ref 1 in
  while !i < Array.length Sys.argv do
    let arg = Sys.argv.(!i) in
    if arg = "--isa" && !i + 1 < Array.length Sys.argv then begin
      isa_arg := Sys.argv.(!i + 1);
      i := !i + 2
    end else begin
      Printf.eprintf "warning: unknown arg %s\n" arg;
      incr i
    end
  done;
  let isas = match !isa_arg with
    | "avx2"   -> ["avx2"]
    | "avx512" -> ["avx512"]
    | "both"   -> ["avx2"; "avx512"]
    | other    -> failwith ("--isa: expected avx2|avx512|both, got " ^ other)
  in
  (* Note: the op counts are *structural* (DAG-level), so they don't
   * actually depend on the ISA target — vec_width changes the emitted
   * loop step but not the number of NK_Add etc. We emit per-ISA tables
   * anyway to match production's layout; entries are identical across
   * tables. If you want per-ISA-distinct counts (e.g. from variants
   * that elide consts via set1 differently per ISA), the math layer
   * would need to thread isa through — not done here for v1. *)
  emit_header ~isas
