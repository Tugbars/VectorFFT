"""
emit_selector.py — generate codelet_select_r32.h from selection.json.

Reads selection.json (winners per region) and produces:
  1. include/<winner>.h for every kept winner (copied from staging/)
  2. include/codelet_select_r32.h — the runtime selector API

The selector is a C function returning a function pointer:
    stride_t1_fn codelet_select_r32_fwd(int isa, size_t ios, size_t me);
    stride_t1_fn codelet_select_r32_bwd(int isa, size_t ios, size_t me);

Implementation: discrete lookup over measured (ios, me) points + a nearest-
point fallback for off-grid calls. Off-grid fallback chosen by Python at
emit time (closest Euclidean point in the (log ios, log me) space), NOT
computed at runtime.

Also emits codelet_select_r32_report.md: human-readable summary of which
codelet wins where.
"""
import argparse
import json
import math
import shutil
import sys
from collections import defaultdict
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))
import candidates


BENCH_ROOT = Path(__file__).parent
STAGING = BENCH_ROOT / 'staging'


# ─────────────────────────────────────────────────────────────────────
# Writing the C selector header
# ─────────────────────────────────────────────────────────────────────

HEADER_PREAMBLE = r"""/* codelet_select_r32.h — auto-generated by emit_selector.py
 *
 * Do NOT edit. Regenerate via bench/emit_selector.py.
 *
 * This file is machine-specific. It was generated on:
 *   {timestamp}
 * with tie_threshold={tie_threshold:.3f}.
 *
 * Usage (in planner / registry setup):
 *   #include "codelet_select_r32.h"
 *   stride_t1_fn fn = codelet_select_r32_fwd(isa, ios, me);
 *
 * Where isa is 0 for AVX2, 1 for AVX-512.
 *
 * Selector strategy:
 *   - For (ios, me) exactly in the measured grid: return the winner.
 *   - For off-grid points: return the winner of the nearest measured
 *     point (Euclidean distance in log2 space). Nearest-point mapping
 *     is baked in at generation time; no runtime search.
 */

#ifndef CODELET_SELECT_R32_H
#define CODELET_SELECT_R32_H

#include <stddef.h>

#define CODELET_ISA_AVX2    0
#define CODELET_ISA_AVX512  1

typedef void (*codelet_r32_t1_fn)(
    double *rio_re, double *rio_im,
    const double *W_re, const double *W_im,
    size_t ios, size_t me);

{forward_decls}

"""

HEADER_EPILOGUE = "\n#endif /* CODELET_SELECT_R32_H */\n"


def _isa_to_int(isa: str) -> int:
    return {'avx2': 0, 'avx512': 1}[isa]


def _id_to_function_name(cid: str, direction: str) -> str:
    """Find the Candidate object with this id, return its fwd/bwd name."""
    for c in candidates.enumerate_all():
        if c.id() == cid:
            return c.function_names()[direction]
    raise ValueError(f"unknown candidate id: {cid}")


def _build_decision_table(decisions: list, isa: str, direction: str) -> list:
    """Extract (ios, me, function_name) rows for a given (isa, direction)."""
    rows = []
    for d in decisions:
        if d['isa'] != isa or d['direction'] != direction:
            continue
        fn = _id_to_function_name(d['winner'], direction)
        rows.append((d['ios'], d['me'], fn))
    # Sort by (me, ios) for stable output
    rows.sort(key=lambda r: (r[1], r[0]))
    return rows


def _emit_table_array(rows: list, table_name: str) -> str:
    """Emit the C decision table as a static const array."""
    if not rows:
        return (f"/* {table_name}: no data */\n"
                f"static const codelet_select_r32_entry_t {table_name}[] = {{\n"
                f"    {{0, 0, NULL}}\n"
                f"}};\n"
                f"static const int {table_name}_count = 0;\n")
    lines = [f"static const codelet_select_r32_entry_t {table_name}[] = {{"]
    for ios, me, fn in rows:
        lines.append(f"    {{{ios}, {me}, {fn}}},")
    lines.append("};")
    lines.append(f"static const int {table_name}_count = "
                 f"{len(rows)};")
    return '\n'.join(lines) + '\n'


SELECTOR_BODY = r"""
/* Decision table entry */
typedef struct {
    size_t ios;
    size_t me;
    codelet_r32_t1_fn fn;
} codelet_select_r32_entry_t;

{tables}

/* Nearest-point search in log2 space. Linear scan — tables are small. */
static codelet_r32_t1_fn
_codelet_select_r32_nearest(const codelet_select_r32_entry_t *tbl,
                             int count, size_t ios, size_t me) {
    if (count == 0) return NULL;
    /* Use integer distance in log2 space: floor(log2(x)).
     * Good enough for selector, avoids math.h dependency. */
    int lios = 0, lme = 0, t;
    for (t = (int)ios; t > 1; t >>= 1) lios++;
    for (t = (int)me;  t > 1; t >>= 1) lme++;

    int best = -1;
    int best_dist = 1000000;
    for (int i = 0; i < count; i++) {
        int eios = 0, eme = 0;
        for (t = (int)tbl[i].ios; t > 1; t >>= 1) eios++;
        for (t = (int)tbl[i].me;  t > 1; t >>= 1) eme++;
        int di = lios - eios; if (di < 0) di = -di;
        int dm = lme  - eme;  if (dm < 0) dm = -dm;
        int dist = di + dm;
        /* Exact match short-circuits */
        if (tbl[i].ios == ios && tbl[i].me == me) return tbl[i].fn;
        if (dist < best_dist) { best_dist = dist; best = i; }
    }
    return best >= 0 ? tbl[best].fn : NULL;
}

/* Public selector API */
static inline codelet_r32_t1_fn
codelet_select_r32_fwd(int isa, size_t ios, size_t me) {
    const codelet_select_r32_entry_t *tbl;
    int count;
    if (isa == CODELET_ISA_AVX512) {
        tbl = table_avx512_fwd; count = table_avx512_fwd_count;
    } else {
        tbl = table_avx2_fwd;   count = table_avx2_fwd_count;
    }
    return _codelet_select_r32_nearest(tbl, count, ios, me);
}

static inline codelet_r32_t1_fn
codelet_select_r32_bwd(int isa, size_t ios, size_t me) {
    const codelet_select_r32_entry_t *tbl;
    int count;
    if (isa == CODELET_ISA_AVX512) {
        tbl = table_avx512_bwd; count = table_avx512_bwd_count;
    } else {
        tbl = table_avx2_bwd;   count = table_avx2_bwd_count;
    }
    return _codelet_select_r32_nearest(tbl, count, ios, me);
}
"""


def emit_selector_h(selection_path: Path, out_dir: Path,
                     verbose: bool = True) -> None:
    selection = json.loads(selection_path.read_text(encoding='utf-8'))
    decisions = selection['decisions']

    out_dir.mkdir(parents=True, exist_ok=True)

    # 1. Copy winning .h files to include/.
    # shutil.copyfile copies raw bytes — no encoding reinterpretation.
    # This matters because the generator's .h files may be in the
    # system's native encoding (cp1252 on Windows) not UTF-8.
    for winner_id in selection['winners']:
        src = STAGING / f'{winner_id}.h'
        dst = out_dir / f'{winner_id}.h'
        shutil.copyfile(src, dst)

    # 2. Build forward-decls section
    forward_decls = ['#include "' + f'{w}.h"' for w in selection['winners']]
    forward_decls_str = '\n'.join(forward_decls)

    # 3. Build per-(isa, dir) tables
    tables = []
    for isa in ('avx2', 'avx512'):
        for direction in ('fwd', 'bwd'):
            rows = _build_decision_table(decisions, isa, direction)
            table_name = f'table_{isa}_{direction}'
            tables.append(_emit_table_array(rows, table_name))
    tables_str = '\n'.join(tables)

    # 4. Assemble header
    from datetime import datetime
    timestamp = datetime.now().isoformat(timespec='seconds')
    preamble = (
        HEADER_PREAMBLE
            .replace('{timestamp}', timestamp)
            .replace('{tie_threshold:.3f}',
                     f'{selection["tie_threshold"]:.3f}')
            .replace('{forward_decls}', forward_decls_str)
    )
    body = SELECTOR_BODY.replace('{tables}', tables_str)
    header = preamble + body + HEADER_EPILOGUE

    out_path = out_dir / 'codelet_select_r32.h'
    # Explicit UTF-8: the header contains em-dashes and other Unicode
    # in its banner comments. Writing without encoding defaults to cp1252
    # on Windows, which fails on any non-ASCII character.
    out_path.write_text(header, encoding='utf-8')

    if verbose:
        print(f"[emit] wrote {out_path}")
        print(f"[emit] copied {len(selection['winners'])} winning .h files")


# ─────────────────────────────────────────────────────────────────────
# Report (human-readable)
# ─────────────────────────────────────────────────────────────────────

def emit_report(selection_path: Path, measurements_path: Path,
                out_path: Path, verbose: bool = True) -> None:
    selection = json.loads(selection_path.read_text(encoding='utf-8'))

    # Load measurements (JSONL or JSON format)
    mtxt = measurements_path.read_text(encoding='utf-8')
    meas_idx = {}
    if measurements_path.suffix == '.jsonl' or (
            mtxt.lstrip().startswith('{') and '\n{' in mtxt.strip()):
        # JSONL: one record per line
        for line in mtxt.splitlines():
            line = line.strip()
            if not line:
                continue
            r = json.loads(line)
            key = (r['id'], r['ios'], r['me'])
            meas_idx.setdefault(key, {'id': r['id'], 'ios': r['ios'], 'me': r['me']})
            meas_idx[key][f"{r['dir']}_ns"] = r['ns']
    else:
        measurements = json.loads(mtxt)
        for m in measurements['measurements']:
            meas_idx[(m['id'], m['ios'], m['me'])] = m

    # Win counts per candidate
    win_counts = defaultdict(int)
    for d in selection['decisions']:
        win_counts[d['winner']] += 1

    lines = [
        '# Codelet Selection Report (R=32)',
        '',
        f'Tie threshold: **{selection["tie_threshold"]:.1%}**',
        f'Total decisions: **{len(selection["decisions"])}**',
        f'Distinct winners: **{len(selection["winners"])}**',
        '',
        '## Win counts',
        '',
        '| Candidate | Regions won |',
        '|-----------|-------------|',
    ]
    for cid, count in sorted(win_counts.items(), key=lambda x: -x[1]):
        lines.append(f'| `{cid}` | {count} |')

    lines += ['', '## Decisions by (isa, direction)', '']

    decisions_by_key = defaultdict(list)
    for d in selection['decisions']:
        decisions_by_key[(d['isa'], d['direction'])].append(d)

    for (isa, dir), ds in sorted(decisions_by_key.items()):
        lines.append(f'### {isa} / {dir}')
        lines.append('')
        lines.append('| ios | me | winner | ns |')
        lines.append('|-----|-----|--------|-----|')
        for d in sorted(ds, key=lambda x: (x['me'], x['ios'])):
            key = (d['winner'], d['ios'], d['me'])
            ns = meas_idx[key][f'{dir}_ns'] if key in meas_idx else 0
            lines.append(
                f'| {d["ios"]} | {d["me"]} | '
                f'`{d["winner"]}` | {ns:.0f} |'
            )
        lines.append('')

    out_path.write_text('\n'.join(lines), encoding='utf-8')
    if verbose:
        print(f"[emit] wrote {out_path}")


# ─────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--selection', default='selection.json')
    ap.add_argument('--measurements', default=None,
                    help='measurements file (auto-detects jsonl vs json)')
    ap.add_argument('--out-dir', default='include',
                    help='output directory for codelet_select_r32.h + winner .h files')
    ap.add_argument('--report', default='codelet_select_r32_report.md')
    ap.add_argument('--quiet', action='store_true')
    args = ap.parse_args()
    verbose = not args.quiet

    # Auto-detect measurements format
    if args.measurements is None:
        if Path('measurements.jsonl').exists():
            args.measurements = 'measurements.jsonl'
        else:
            args.measurements = 'measurements.json'

    emit_selector_h(Path(args.selection), Path(args.out_dir), verbose=verbose)
    emit_report(Path(args.selection), Path(args.measurements),
                Path(args.report), verbose=verbose)


if __name__ == '__main__':
    main()