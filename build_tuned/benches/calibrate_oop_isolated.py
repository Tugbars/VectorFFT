#!/usr/bin/env python3
"""calibrate_oop_isolated.py — per-cell isolated OOP calibration driver.

The in-process sweep in calibrate_oop.exe mis-picks the kind on cells where
MODEB and the native (LEAF/BAILEY2) champion are close: 31 cells back-to-back
in one process leave cross-cell cache/thermal carryover that biases the
same-binary rdtsc comparison (measured: N=64 K=256 picked BAILEY2 in-sweep ->
0.767x MKL, but MODEB in isolation -> 2.119x). This driver runs ONE cell per
fresh process (VFFT_CAL_N/VFFT_CAL_K), exactly how run_bench.py drives the
bench, so each kind comparison starts from a cold, uncontended state.

Cells = the pow2 (N>=8), K%8==0 cells present in the in-place c2c wisdom.
Output accumulates into the generated oop_wisdom.txt (header written once here,
each cell appended by the isolated calibrate_oop process).

Run:  cd build_tuned/benches && python calibrate_oop_isolated.py
"""
import os, re, subprocess, sys

HERE   = os.path.dirname(os.path.abspath(__file__))
EXE    = os.path.join(HERE, "calibrate_oop.exe")
INPLACE= os.path.normpath(os.path.join(
    HERE, "../../src/dag-fft-compiler/generator/generated/spike_wisdom.txt"))
OUT    = os.path.normpath(os.path.join(
    HERE, "../../src/dag-fft-compiler/generator/generated/oop_wisdom.txt"))

def is_pow2(n): return n >= 8 and (n & (n - 1)) == 0

def cells():
    seen, out = set(), []
    with open(INPLACE) as f:
        for line in f:
            line = line.strip()
            if not line or line[0] in "#@":
                continue
            t = line.split()
            try:
                N, K = int(t[0]), int(t[1])
            except (ValueError, IndexError):
                continue
            if is_pow2(N) and K > 0 and K % 8 == 0 and (N, K) not in seen:
                seen.add((N, K)); out.append((N, K))
    return out

def main():
    cs = cells()
    print(f"== isolated OOP calibration: {len(cs)} pow2/K%8 cells, one process each ==")
    # header once (isolated calibrate_oop appends, never writes the header)
    with open(OUT, "w") as f:
        f.write("# OOP c2c wisdom v2 (generated by calibrate_oop_isolated). Format:\n")
        f.write("#   N K kind [params] ns   (0=LEAF | 1=BAILEY2 R1 R2 t1p | 2=MODEB nf f.. v..)\n")
    for N, K in cs:
        env = dict(os.environ, VFFT_CAL_N=str(N), VFFT_CAL_K=str(K))
        r = subprocess.run([EXE, INPLACE, OUT], env=env,
                           capture_output=True, text=True)
        # echo the single [isolated]/result line(s) the child printed
        for ln in r.stdout.splitlines():
            if ln.lstrip().startswith(("[isolated]", "N=")):
                print(f"  {ln.strip()}")
        if r.returncode != 0:
            print(f"  !! N={N} K={K} failed (rc={r.returncode})\n{r.stderr}", file=sys.stderr)
    print(f"# wrote {OUT}")

if __name__ == "__main__":
    main()
