/* calibrate_oop.c — offline OOP c2c calibrator.
 *
 * For each (N,K) in the grid, run the 2-axis joint chooser
 * (vfft_oop_plan_create_dp_best: tuner-best-native vs DP-best-MODEB), re-time
 * the winner, and append a {kind, factorization} entry to oop_wisdom.txt. The
 * runtime path (vfft_oop_plan_create_wisdom) then does a pure lookup.
 *
 * Build: cd build_tuned && python build.py --src benches/calibrate_oop.c --compile
 * Run  : PATH += C:\mingw152\mingw64\bin ; writes ./oop_wisdom.txt
 */
#define _GNU_SOURCE 1
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "executor.h"
#include "env.h"
#include "oop_dp.h"        /* vfft_oop_plan_create_dp_best */
#include "oop_wisdom.h"    /* entry + writer */

#define PIN_CORE 2
#if defined(_WIN32)
#include <malloc.h>
#define AALLOC(n) _aligned_malloc((n),64)
#define AFREE(p)  _aligned_free(p)
#else
#define AALLOC(n) aligned_alloc(64,(n))
#define AFREE(p)  free(p)
#endif

static const struct { int N; size_t K; } GRID[] = {
    { 64, 512}, {128, 512}, {169, 512}, {256, 256},
    {512, 120}, {1024, 120}, {1024, 256}, {2048, 256},
    {4096, 256}, {2310, 32},
};
static const int NGRID = (int)(sizeof GRID / sizeof GRID[0]);

static double time_winner(vfft_oop_plan_t *p, int N, size_t K) {
    size_t T = (size_t)N * K;
    double *sr = AALLOC(T*8), *si = AALLOC(T*8), *dr = AALLOC(T*8), *di = AALLOC(T*8);
    for (size_t i = 0; i < T; i++) { sr[i] = (double)(i%251)*0.013-1.6; si[i] = (double)(i%257)*0.011-1.4; }
    for (int w = 0; w < 5; w++) vfft_oop_execute_fwd(p, sr, si, dr, di);
    int reps = (int)(8e6/(T+1)); if (reps < 20) reps = 20; if (reps > 50000) reps = 50000;
    double best = 1e18;
    for (int t = 0; t < 15; t++) {
        double t0 = vfft_proto_now_ns();
        for (int i = 0; i < reps; i++) vfft_oop_execute_fwd(p, sr, si, dr, di);
        double ns = (vfft_proto_now_ns()-t0)/reps; if (ns < best) best = ns;
    }
    AFREE(sr); AFREE(si); AFREE(dr); AFREE(di);
    return best;
}

int main(void) {
    stride_env_init();
    if (stride_pin_thread(PIN_CORE) != 0) fprintf(stderr, "warn: pin cpu%d failed\n", PIN_CORE);

    vfft_proto_registry_t reg; vfft_proto_registry_init(&reg);
    const char *out_path = "oop_wisdom.txt";
    FILE *out = fopen(out_path, "w");
    if (!out) { fprintf(stderr, "cannot open %s for write\n", out_path); return 1; }
    fprintf(out, "# OOP c2c wisdom (generated by calibrate_oop). Format:\n");
    fprintf(out, "#   N K kind [params] ns   (kind 0=LEAF | 1=BAILEY2 R1 R2 | 2=MODEB nf f0..)\n");

    printf("== OOP calibration (2-axis dp_best) -> %s ==\n", out_path);
    for (int g = 0; g < NGRID; g++) {
        int N = GRID[g].N; size_t K = GRID[g].K;
        vfft_proto_dp_context_t ctx; vfft_proto_dp_init(&ctx, K, N);
        vfft_oop_plan_t *p = vfft_oop_plan_create_dp_best(N, K, &ctx, &reg);
        if (!p) {
            printf("  N=%-5d K=%-4zu  no plan (skipped)\n", N, K);
            vfft_proto_dp_destroy(&ctx);
            continue;
        }
        double ns = time_winner(p, N, K);
        vfft_oop_wisdom_entry_t e;
        vfft_oop_wisdom_entry_from_plan(&e, p, N, K, ns);
        vfft_oop_wisdom_write_entry(out, &e);
        fflush(out);
        const char *kn = e.kind==VFFT_OOP_KIND_LEAF?"LEAF":e.kind==VFFT_OOP_KIND_BAILEY2?"BAILEY2":"MODEB";
        char fs[96]; size_t o=0; fs[0]='\0';
        if (e.kind==VFFT_OOP_KIND_BAILEY2) snprintf(fs,sizeof fs,"%dx%d",e.R1,e.R2);
        else if (e.kind==VFFT_OOP_KIND_MODEB) for(int s=0;s<e.nf;s++) o+=(size_t)snprintf(fs+o,sizeof fs-o,"%s%d",s?",":"",e.factors[s]);
        printf("  N=%-5d K=%-4zu -> %-7s %-14s  %.1f ns\n", N, K, kn, fs, ns);
        vfft_oop_plan_destroy(p);
        vfft_proto_dp_destroy(&ctx);
    }
    fclose(out);
    printf("# wrote %s\n", out_path);
    return 0;
}
