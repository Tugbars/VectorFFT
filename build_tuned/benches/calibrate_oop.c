/* calibrate_oop.c — offline OOP c2c calibrator (v2, variant-rich).
 *
 * For each pow2 (N,K) present in the in-place c2c wisdom (K % 8 == 0), run the
 * 2-axis joint chooser and persist {kind, factorization, per-stage variants} to
 * oop_wisdom.txt. The runtime path (vfft_oop_plan_create_wisdom) then does a
 * pure lookup + build.
 *
 * The two kinds are sourced exactly like the in-place engine:
 *   - MODEB   = the in-place c2c wisdom entry's (factors, variants), built OOP.
 *               MODEB literally runs the in-place DIT dataflow out-of-place, so
 *               it inherits the in-place patient search's FLAT/T1S/LOG3 mix for
 *               free. DIF-preferring cells can't be MODEB (OOP stage 0 must be
 *               untwiddled) and fall back to the native champion.
 *   - native  = the BAILEY2 pair tuner, now measuring flat-vs-log3 t1p per pair,
 *               plus LEAF at small N.
 * Axis 1 (kind): time the two champions, keep the faster.
 *
 * Build: cd build_tuned && python build.py --src benches/calibrate_oop.c --compile
 * Run  : PATH += C:\mingw152\mingw64\bin ; writes the generated/ oop_wisdom.txt
 *        (same folder as every other wisdom: spike/rfft/c2r/bluestein).
 */
#define _GNU_SOURCE 1
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "executor.h"
#include "env.h"
#include "wisdom_reader.h"  /* in-place c2c wisdom (factors + variants) */
#include "oop_auto.h"       /* vfft_oop_tune_pairs_v + plan create */
#include "oop_wisdom.h"     /* entry + writer */

#define PIN_CORE 2
#if defined(_WIN32)
#include <malloc.h>
#define AALLOC(n) _aligned_malloc((n),64)
#define AFREE(p)  _aligned_free(p)
#else
#define AALLOC(n) aligned_alloc(64,(n))
#define AFREE(p)  free(p)
#endif

/* In-place c2c wisdom — the source of the pow2 cells AND the MODEB variants.
 * Override with argv[1]. */
#ifndef OOP_CAL_INPLACE_WISDOM
#define OOP_CAL_INPLACE_WISDOM "../../src/dag-fft-compiler/generator/generated/spike_wisdom.txt"
#endif

static double time_plan(vfft_oop_plan_t *p, int N, size_t K) {
    size_t T = (size_t)N * K;
    double *sr = AALLOC(T*8), *si = AALLOC(T*8), *dr = AALLOC(T*8), *di = AALLOC(T*8);
    for (size_t i = 0; i < T; i++) { sr[i] = (double)(i%251)*0.013-1.6; si[i] = (double)(i%257)*0.011-1.4; }
    for (int w = 0; w < 5; w++) vfft_oop_execute_fwd(p, sr, si, dr, di);
    int reps = (int)(8e6/(T+1)); if (reps < 20) reps = 20; if (reps > 50000) reps = 50000;
    double best = 1e18;
    for (int t = 0; t < 15; t++) {
        double t0 = vfft_proto_now_ns();
        for (int i = 0; i < reps; i++) vfft_oop_execute_fwd(p, sr, si, dr, di);
        double ns = (vfft_proto_now_ns()-t0)/reps; if (ns < best) best = ns;
    }
    AFREE(sr); AFREE(si); AFREE(dr); AFREE(di);
    return best;
}

/* Native champion: pair tuner (flat-vs-log3 t1p) or LEAF. NULL if none. */
static vfft_oop_plan_t *build_native(int N, size_t K, const vfft_proto_registry_t *reg) {
    int r1 = 0, r2 = 0, t1p = 1;
    int nc = vfft_oop_tune_pairs_v(N, (size_t)K, &r1, &r2, &t1p, 0);
    if (nc <= 0) return NULL;
    if (r1 == 0)  /* LEAF won the tuner */
        return vfft_oop_plan_create(N, (size_t)K, NULL, 0, (vfft_proto_registry_t *)reg);
    return vfft_oop_plan_create_pair_v(N, (size_t)K, r1, r2, t1p);
}

int main(int argc, char **argv) {
    const char *wpath    = (argc >= 2) ? argv[1] : OOP_CAL_INPLACE_WISDOM;
    const char *out_path = (argc >= 3) ? argv[2]
        : "../../src/dag-fft-compiler/generator/generated/oop_wisdom.txt";

    stride_env_init();
    if (stride_pin_thread(PIN_CORE) != 0) fprintf(stderr, "warn: pin cpu%d failed\n", PIN_CORE);
    vfft_proto_registry_t reg; vfft_proto_registry_init(&reg);

    vfft_proto_wisdom_t w;
    if (vfft_proto_wisdom_load(&w, wpath) != 0) {
        fprintf(stderr, "cannot open in-place wisdom %s\n", wpath); return 1;
    }
    FILE *out = fopen(out_path, "w");
    if (!out) { fprintf(stderr, "cannot open %s for write\n", out_path); vfft_proto_wisdom_free(&w); return 1; }
    fprintf(out, "# OOP c2c wisdom v2 (generated by calibrate_oop). Format:\n");
    fprintf(out, "#   N K kind [params] ns   (0=LEAF | 1=BAILEY2 R1 R2 t1p | 2=MODEB nf f.. v..)\n");

    printf("== OOP calibration v2 (MODEB<-in-place variants, BAILEY2 flat/log3 search) ==\n");
    printf("== source: %s -> %s ==\n", wpath, out_path);
    int done = 0, skipped = 0;
    for (size_t i = 0; i < w.count; i++) {
        const vfft_proto_wisdom_entry_t *ipe = &w.entries[i];
        int N = ipe->N; long K = (long)ipe->K;
        if (N < 8 || (N & (N - 1)) != 0) continue;        /* pow2 N only */
        if (K <= 0 || (K % 8) != 0) continue;             /* OOP lane contract (skips K=4) */

        /* MODEB straight from the in-place entry's variant-rich plan (DIT only). */
        vfft_oop_plan_t *modeb = NULL;
        if (ipe->use_dif_forward == 0)
            modeb = _vfft_oop_make_modeb(N, (size_t)K, ipe->factors, ipe->variants, ipe->nf, &reg);

        /* Native champion (LEAF / best BAILEY2 pair + t1p variant). */
        vfft_oop_plan_t *native = build_native(N, (size_t)K, &reg);

        if (!modeb && !native) {
            printf("  N=%-5d K=%-4ld  no plan (skipped)\n", N, K);
            skipped++; continue;
        }

        /* Axis 1: time both champions, keep the faster. */
        double tm = modeb  ? time_plan(modeb,  N, (size_t)K) : 1e18;
        double tn = native ? time_plan(native, N, (size_t)K) : 1e18;
        vfft_oop_plan_t *win = (tm <= tn) ? modeb : native;
        vfft_oop_plan_t *lose = (tm <= tn) ? native : modeb;
        double ns = (tm <= tn) ? tm : tn;
        if (lose) vfft_oop_plan_destroy(lose);

        vfft_oop_wisdom_entry_t e;
        vfft_oop_wisdom_entry_from_plan(&e, win, N, (size_t)K, ns);
        vfft_oop_wisdom_write_entry(out, &e);
        fflush(out);

        const char *kn = e.kind==VFFT_OOP_KIND_LEAF?"LEAF":e.kind==VFFT_OOP_KIND_BAILEY2?"BAILEY2":"MODEB";
        char fs[160]; size_t o = 0; fs[0] = '\0';
        if (e.kind == VFFT_OOP_KIND_BAILEY2)
            snprintf(fs, sizeof fs, "%dx%d/%s", e.R1, e.R2, e.t1p_variant ? "log3" : "flat");
        else if (e.kind == VFFT_OOP_KIND_MODEB) {
            for (int s = 0; s < e.nf; s++) o += (size_t)snprintf(fs+o, sizeof fs-o, "%s%d", s?",":"", e.factors[s]);
            o += (size_t)snprintf(fs+o, sizeof fs-o, " v[");
            for (int s = 0; s < e.nf; s++) o += (size_t)snprintf(fs+o, sizeof fs-o, "%s%d", s?",":"", e.variants[s]);
            snprintf(fs+o, sizeof fs-o, "]");
        }
        printf("  N=%-5d K=%-4ld -> %-7s %-28s  %.1f ns\n", N, K, kn, fs, ns);
        vfft_oop_plan_destroy(win); done++;
    }
    fclose(out); vfft_proto_wisdom_free(&w);
    printf("# wrote %s  (%d pow2 cells calibrated, %d skipped)\n", out_path, done, skipped);
    return 0;
}
